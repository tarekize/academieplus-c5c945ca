# درس: التوفيقات ودستور ثنائي الحد

**التوفيقات** تعدّ الطرق لاختيار مجموعة فرعية دون اعتبار الترتيب.

## 1. التوفيقات

::: definition
**تعريف 1.1**

عدد طرق اختيار $k$ عنصراً من $n$ عنصراً (دون ترتيب، دون تكرار):

$$\boxed{C_n^k = \binom{n}{k} = \frac{n!}{k!(n-k)!}}$$
:::

::: property
**⭐ خواص أساسية**

| الخاصية | الصيغة | تفسير |
|---|---|---|
| الحالات الطرفية | $C_n^0 = C_n^n = 1$ | مجموعة فارغة / كاملة |
| الحد الأول | $C_n^1 = n$ | اختيار عنصر واحد |
| التناظر | $C_n^k = C_n^{n-k}$ | اختيار $k$ = استبعاد $n-k$ |
| علاقة باسكال | $C_n^k + C_n^{k+1} = C_{n+1}^{k+1}$ | |
:::

::: example
**📝 حساب $C_n^k$:**

$C_5^2 = \dfrac{5!}{2!3!} = \dfrac{5 \times 4}{2} = 10$

$C_{10}^3 = \dfrac{10 \times 9 \times 8}{3!} = \dfrac{720}{6} = 120$

$C_{20}^0 = 1$، $C_{20}^1 = 20$، $C_{20}^{19} = 20$، $C_{20}^{20} = 1$.
:::

## 2. مثلث باسكال

::: graphic
