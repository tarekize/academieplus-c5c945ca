# درس: القانون الأسي

القانون الأسي يُنمذج **أزمنة الانتظار** ومدد الحياة في سياقات كثيرة.

## 1. التعريف

::: definition
**تعريف 1.1 — $\mathcal{E}(\lambda)$**

$$\boxed{f(x) = \begin{cases}\lambda e^{-\lambda x} & x \geq 0\\ 0 & x < 0\end{cases}, \quad \lambda > 0}$$

**التحقق:** $\int_0^{+\infty}\lambda e^{-\lambda x}\,dx = [-e^{-\lambda x}]_0^{+\infty} = 1$ ✓
:::

## 2. دالة التوزيع والاحتمالات

::: property
**⭐ الصيغ الأساسية**

$$\boxed{F(t) = P(X \leq t) = 1 - e^{-\lambda t}} \qquad P(X > t) = e^{-\lambda t}$$

$$P(a \leq X \leq b) = e^{-\lambda a} - e^{-\lambda b}$$
:::

## 3. الخواص

::: property
**⭐ الأمل والتباين**

$$\boxed{E(X) = \frac{1}{\lambda}} \qquad V(X) = \frac{1}{\lambda^2} \qquad \sigma(X) = \frac{1}{\lambda}$$

**ملاحظة مميّزة:** $E(X) = \sigma(X) = 1/\lambda$ (خاصية فريدة للقانون الأسي).
:::

::: property
**⭐ خاصية اللاذاكرة (Memoryless Property)**

$$\boxed{P(X > s+t \mid X > s) = P(X > t)}$$

**البرهان:**
$$P(X>s+t|X>s) = \frac{P(X>s+t)}{P(X>s)} = \frac{e^{-\lambda(s+t)}}{e^{-\lambda s}} = e^{-\lambda t} = P(X>t)$$

**التفسير:** الجهاز لا "يشيخ" — إذا عمل حتى الآن، احتمال استمراره يظل نفسه.
:::

## 4. التمثيل البياني

::: graphic
**📈 خواص المنحنى**

- $f(0) = \lambda$ (القيمة الابتدائية).
- $f$ متناقصة أسياً.
- $\lambda$ كبير → انحدار سريع، $E$ صغير.
- $F(x)$ يرتفع من $0$ نحو $1$ بشكل أسي.

**مقارنة:** $\lambda = 0{,}5$: $E = 2$؛ $\lambda = 2$: $E = 0{,}5$.
:::

## 5. أمثلة تطبيقية شاملة

::: example
**📝 المثال 1 — مصباح كهربائي:**

$X \sim \mathcal{E}(0{,}02)$ (ساعة).

أ) $E(X) = 1/0{,}02 = 50$ ساعة.

ب) $P(X > 100) = e^{-2} \approx 13{,}5\%$.

ج) $P(X > 150 | X > 50) = P(X > 100) = e^{-2} \approx 13{,}5\%$ (اللاذاكرة).

د) $P(40 \leq X \leq 60) = e^{-0{,}8} - e^{-1{,}2} \approx 0{,}449 - 0{,}301 = 0{,}148$.
:::

::: example
**📝 المثال 2 — زمن الانتظار:**

$X \sim \mathcal{E}(3)$ (دقيقة). $E(X) = 1/3$ دقيقة ≈ $20$ ثانية.

$P(X > 1) = e^{-3} \approx 5\%$ (الانتظار أكثر من دقيقة نادر).

$P(X \leq 0{,}5) = 1 - e^{-1{,}5} \approx 77{,}7\%$.
:::

::: example
**📝 المثال 3 — تحديد $\lambda$:**

عمر جهاز يتبع $\mathcal{E}(\lambda)$، ونعلم أن $P(X > 2) = 0{,}5$.

$e^{-2\lambda} = 0{,}5 \Rightarrow -2\lambda = \ln(0{,}5) \Rightarrow \lambda = \dfrac{\ln 2}{2} \approx 0{,}347$/سنة.

$E(X) = 1/\lambda = 2/\ln 2 \approx 2{,}885$ سنة.
:::

## 6. علاقة بقانون بواسون

::: remark
**🔑 الصلة النظرية:**

إذا كانت الأحداث تصل وفق **قانون بواسون** بمعدل $\lambda$ حدث/وحدة زمن، فإن الزمن بين حدثين متتاليين يتبع $\mathcal{E}(\lambda)$.

**مثال:** إذا كانت تعطّلات خادم $3$ في الساعة (بواسون)، فالزمن بين عطلين $\sim \mathcal{E}(3)$، أي متوسطه $20$ دقيقة.
:::

## 🎯 خلاصة

$$\boxed{f(x) = \lambda e^{-\lambda x} \quad (x \geq 0)} \qquad \boxed{P(X > t) = e^{-\lambda t}}$$

$$\boxed{E(X) = \sigma(X) = \frac{1}{\lambda}}$$

$$\boxed{P(X > s+t | X > s) = P(X > t) \quad \text{(اللاذاكرة)}}$$

