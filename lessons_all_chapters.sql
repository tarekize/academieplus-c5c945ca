# درس: قوى عدد حقيقي موجب

تُعدّ **قوى الأعداد** حجر الأساس في دراسة الدوال الأسية والدوال الجذرية. في هذا الدرس نعيد تنظيم وتوسيع مفهوم القوى على $\mathbb{R}$.

## 1. القوى الصحيحة

::: definition
**تعريف 1.1 — القوى الصحيحة**

ليكن $a > 0$ و $n \in \mathbb{Z}$:

- إذا $n \geq 1$: $a^n = \underbrace{a \times a \times \cdots \times a}_{n \text{ مرة}}$
- $a^0 = 1$
- إذا $n \leq -1$: $a^n = \dfrac{1}{a^{-n}}$
:::

::: property
**⭐ قواعد الحساب للقوى الصحيحة**

لكل $a > 0$، $b > 0$، $m, n \in \mathbb{Z}$:

| القاعدة | الصيغة | مثال |
|---|---|---|
| جداء بنفس الأساس | $a^m \cdot a^n = a^{m+n}$ | $2^3 \cdot 2^4 = 2^7 = 128$ |
| خارج بنفس الأساس | $\dfrac{a^m}{a^n} = a^{m-n}$ | $3^5/3^2 = 3^3 = 27$ |
| قوة لقوة | $(a^m)^n = a^{mn}$ | $(2^3)^2 = 2^6 = 64$ |
| جداء بنفس الأس | $a^n \cdot b^n = (ab)^n$ | $2^3 \cdot 3^3 = 6^3 = 216$ |
| خارج بنفس الأس | $\dfrac{a^n}{b^n} = \left(\dfrac{a}{b}\right)^n$ | $4^2/2^2 = 2^2 = 4$ |
:::

## 2. القوى الكسرية

::: definition
**تعريف 2.1 — الجذور والقوى الكسرية**

ليكن $a > 0$ و $n \in \mathbb{N}^*$ و $p \in \mathbb{Z}$:

$$\sqrt[n]{a} = a^{1/n} \quad \text{هو العدد الموجب الوحيد الذي } (a^{1/n})^n = a$$

$$a^{p/n} = \left(\sqrt[n]{a}\right)^p = \sqrt[n]{a^p}$$

**الوجود والوحدانية:** لكل $a > 0$ و $n \in \mathbb{N}^*$، يوجد عدد حقيقي موجب وحيد هو $a^{1/n}$.
:::

::: example
**📝 القيم الخاصة والاختزالات:**

| التعبير | الاختزال | القيمة عند $a=4$ |
|---|---|---|
| $\sqrt{a}$ | $a^{1/2}$ | $2$ |
| $\sqrt[3]{a}$ | $a^{1/3}$ | $\approx 1{,}587$ |
| $a^{3/2}$ | $(\sqrt{a})^3$ | $8$ |
| $a^{-1/2}$ | $\dfrac{1}{\sqrt{a}}$ | $0{,}5$ |
| $a^{2/3}$ | $(\sqrt[3]{a})^2$ | $\approx 2{,}52$ |
:::

## 3. القوى الحقيقية

::: definition
**تعريف 3.1 — التعميم إلى $\mathbb{R}$**

ليكن $a > 0$ و $\alpha \in \mathbb{R}$. نُعرّف:

$$\boxed{a^\alpha = e^{\alpha \ln a}}$$

**لماذا هذا التعريف؟** لأن $\ln$ و $\exp$ معرّفتان على جميع الأعداد الحقيقية، وهذا التعريف يتوافق مع القيم الجزئية المعروفة:
- $a^{1/2} = e^{\frac{1}{2}\ln a} = \sqrt{a}$ ✓
- $a^2 = e^{2\ln a} = e^{\ln a^2} = a^2$ ✓
:::

::: property
**⭐ قواعد الحساب للقوى الحقيقية**

لكل $a > 0$، $b > 0$، $\alpha, \beta \in \mathbb{R}$:

$$a^\alpha \cdot a^\beta = a^{\alpha+\beta} \qquad \frac{a^\alpha}{a^\beta} = a^{\alpha-\beta}$$

$$(a^\alpha)^\beta = a^{\alpha\beta} \qquad a^\alpha \cdot b^\alpha = (ab)^\alpha$$

$$a^\alpha > 0 \text{ دائماً} \qquad a^0 = 1$$
:::

::: remark
**🔑 الاشتقاق باستخدام التعريف:**

بما أن $a^\alpha = e^{\alpha \ln a}$، يمكن اشتقاق الدالة $x \mapsto a^x$:
$$\frac{d}{dx}(a^x) = \frac{d}{dx}(e^{x\ln a}) = \ln a \cdot e^{x\ln a} = \ln a \cdot a^x$$
:::

## 4. أمثلة تطبيقية مفصّلة

::: example
**📝 المثال 1:** بسّط $A = \dfrac{8^{2/3} \cdot 4^{3/2}}{2^5}$.

**✅ الحل المفصّل:**

نكتب كل شيء كقوة للعدد $2$:
$$8 = 2^3 \Rightarrow 8^{2/3} = (2^3)^{2/3} = 2^{3 \times 2/3} = 2^2 = 4$$
$$4 = 2^2 \Rightarrow 4^{3/2} = (2^2)^{3/2} = 2^{2 \times 3/2} = 2^3 = 8$$
$$A = \frac{4 \times 8}{2^5} = \frac{2^2 \times 2^3}{2^5} = \frac{2^5}{2^5} = 1$$
:::

::: example
**📝 المثال 2:** بسّط $B = \dfrac{\sqrt[3]{a^2} \cdot \sqrt{a^3}}{a^{7/6}}$ مع $a > 0$.

**✅ الحل المفصّل:**

نحوّل كل جذر إلى قوة كسرية:
$$\sqrt[3]{a^2} = a^{2/3}, \quad \sqrt{a^3} = a^{3/2}$$

$$B = \frac{a^{2/3} \cdot a^{3/2}}{a^{7/6}} = a^{2/3 + 3/2 - 7/6}$$

نجد قاسماً مشتركاً للأسس ($\text{م.م.أ} = 6$):
$$\frac{2}{3} + \frac{3}{2} - \frac{7}{6} = \frac{4}{6} + \frac{9}{6} - \frac{7}{6} = \frac{4+9-7}{6} = \frac{6}{6} = 1$$

$$\boxed{B = a^1 = a}$$
:::

::: example
**📝 المثال 3:** أوجد قيمة $\left(\dfrac{27}{8}\right)^{-2/3}$.

**✅ الحل:**

$$\left(\frac{27}{8}\right)^{-2/3} = \left(\frac{8}{27}\right)^{2/3} = \frac{8^{2/3}}{27^{2/3}} = \frac{(2^3)^{2/3}}{(3^3)^{2/3}} = \frac{2^2}{3^2} = \frac{4}{9}$$
:::

::: example
**📝 المثال 4 (تكميلي):** حلّ المعادلة $x^{3/2} = 8$ في $\mathbb{R}^+$.

**✅ الحل:**

$$x^{3/2} = 8 \iff (x^{1/2})^3 = 8 \iff x^{1/2} = 2 \iff x = 4$$

**تحقق:** $4^{3/2} = (4^{1/2})^3 = 2^3 = 8$ ✓
:::

::: remark
**⚠️ تنبيهات مهمة:**

1. $a^\alpha$ معرّف فقط لـ $a > 0$ عندما $\alpha \notin \mathbb{Z}$ (في $\mathbb{R}$).
2. $(-2)^{1/2}$ غير معرّف في $\mathbb{R}$ (معرّف في $\mathbb{C}$ فقط).
3. $(-8)^{1/3} = -2$ حالة خاصة (مقام فردي)، لكن لا تُعمّم.
4. $0^\alpha = 0$ لكل $\alpha > 0$، و $0^0$ غير معرّف.
:::

## 5. مقارنة القوى

::: property
**⭐ ترتيب القوى**

لكل $a > 1$ و $\alpha, \beta \in \mathbb{R}$:
$$a^\alpha < a^\beta \iff \alpha < \beta \quad \text{(الدالة $x\mapsto a^x$ متزايدة)}$$

لكل $0 < a < 1$ و $\alpha, \beta \in \mathbb{R}$:
$$a^\alpha < a^\beta \iff \alpha > \beta \quad \text{(الدالة $x\mapsto a^x$ متناقصة)}$$
:::

::: example
**📝 المثال 5:** رتّب بالتزايد: $2^{1/2}$، $2^{3/4}$، $2^{-1}$، $2^{1/3}$، $2^0$.

**✅ الحل:** بما أن $2 > 1$، نرتّب الأسس تزايداً:
$$-1 < 0 < \frac{1}{3} < \frac{1}{2} < \frac{3}{4}$$
$$\Rightarrow 2^{-1} < 2^0 < 2^{1/3} < 2^{1/2} < 2^{3/4}$$
$$\Rightarrow \frac{1}{2} < 1 < \sqrt[3]{2} < \sqrt{2} < 2^{3/4}$$
:::

## 6. تطبيقات في الحياة العملية

::: example
**📝 تطبيق — مقياس ريختر للزلازل:**

قوة زلزال بمقياس ريختر تُقاس بـ $M = \log_{10}(I)$ حيث $I$ شدة الزلزال.

زلزال بقوة $M_1 = 6$ وآخر بقوة $M_2 = 8$. نسبة الشدتين:

$$\frac{I_2}{I_1} = \frac{10^8}{10^6} = 10^{8-6} = 10^2 = 100$$

الزلزال الثاني أقوى بـ $100$ مرة!
:::

## 🎯 خلاصة

$$\boxed{a^\alpha = e^{\alpha \ln a}}$$

- $a^{\alpha+\beta} = a^\alpha \cdot a^\beta$؛ $(a^\alpha)^\beta = a^{\alpha\beta}$؛ $a^\alpha \cdot b^\alpha = (ab)^\alpha$.
- $a^\alpha > 0$ دائماً إذا $a > 0$.
- إذا $a > 1$: $\alpha \mapsto a^\alpha$ متزايدة.
- إذا $0 < a < 1$: $\alpha \mapsto a^\alpha$ متناقصة.
