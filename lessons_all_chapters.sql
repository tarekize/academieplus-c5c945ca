# درس: الحوادث المستقلة والمتغيرات العشوائية المستقلة

## 1. الاستقلالية بين حادثتين

::: definition
**تعريف 1.1**

$A$ و $B$ **مستقلتان** إذا وفقط إذا:

$$\boxed{P(A \cap B) = P(A) \times P(B)}$$

أيضاً: $P(A|B) = P(A)$ (إذا $P(B) > 0$).
:::

::: remark
**⚠️ التفريق بين المفاهيم:**

| المفهوم | الشرط | التفسير |
|---|---|---|
| مستقلتان | $P(A\cap B) = P(A)P(B)$ | معرفة $B$ لا تغيّر احتمال $A$ |
| متنافيتان | $A \cap B = \emptyset$ | لا يمكن وقوعهما معاً |
| متممتان | $A = \bar{B}$ | إحداهما تقع ↔ الأخرى لا |

**حادثتان متنافيتان وغير صفريتين هما غير مستقلتين أبداً!**
(لأن $P(A)P(B) > 0$ بينما $P(A\cap B) = 0$)
:::

::: example
**📝 المثال 1:** حجر نرد متوازن. $A = \{2,4,6\}$ (زوجي)، $B = \{1,2,3\}$ (أقل من 4).

$P(A) = 1/2$، $P(B) = 1/2$، $A \cap B = \{2\}$، $P(A \cap B) = 1/6$.

$P(A) \times P(B) = 1/4 \neq 1/6$. **$A$ و $B$ غير مستقلتين**.
:::

::: example
**📝 المثال 2:** نرمي حجرين مختلفين. $A$ = "الأول زوجي"، $B$ = "الثاني زوجي".

$P(A) = P(B) = 1/2$، $P(A \cap B) = 1/4 = P(A)P(B)$. **$A$ و $B$ مستقلتان** ✓
:::

## 2. استقلالية عدة حوادث

::: definition
**تعريف 2.1 — الاستقلالية الكاملة**

الحوادث $A_1, \ldots, A_n$ مستقلة بشكل متبادل إذا لكل مجموعة جزئية:
$$P\!\left(\bigcap_{i \in I} A_i\right) = \prod_{i \in I} P(A_i)$$
:::

## 3. التجارب المستقلة المتكررة

::: property
**⭐ نموذج التجارب المستقلة**

عند تكرار نفس التجربة $n$ مرة باستقلالية تامة:
- احتمال أي تسلسل محدد من النتائج = جداء الاحتمالات.
:::

::: example
**📝 المثال 3:** نرمي عملة متوازنة $5$ مرات. ما احتمال الحصول على $3$ وجوه بالضبط؟

احتمال تسلسل محدد (مثل $HHHTT$) = $(1/2)^5 = 1/32$.

عدد التسلسلات الممكنة = $C_5^3 = 10$.

$$P(\text{3 وجوه}) = C_5^3 \times (1/2)^5 = 10/32 = 5/16$$

(هذا بداية قانون ثنائي الحد!)
:::

::: example
**📝 المثال 4:** جهازان مستقلان، موثوقيات $R_1 = 0{,}9$، $R_2 = 0{,}8$.

**التوصيل على التوالي** (يعمل النظام إذا عملا معاً):
$$P(\text{النظام يعمل}) = R_1 \times R_2 = 0{,}9 \times 0{,}8 = 0{,}72$$

**التوصيل على التوازي** (يعمل النظام إذا عمل أحدهما):
$$P(\text{النظام يعمل}) = 1 - P(\bar{A_1} \cap \bar{A_2}) = 1 - 0{,}1 \times 0{,}2 = 0{,}98$$
:::

## 4. المتغيران العشوائيان المستقلان

::: definition
**تعريف 4.1**

$X$ و $Y$ **مستقلان** إذا لكل $x, y$:
$$P(X=x \text{ و } Y=y) = P(X=x) \times P(Y=y)$$
:::

::: property
**⭐ نتائج الاستقلالية**

- $E(XY) = E(X) \cdot E(Y)$
- $V(X+Y) = V(X) + V(Y)$
- $\sigma(X+Y) = \sqrt{V(X) + V(Y)}$
:::

::: example
**📝 المثال 5:** $X$ و $Y$ مستقلان، $E(X) = 2$، $E(Y) = 3$، $V(X) = 1$، $V(Y) = 4$.

$E(X+Y) = 5$، $E(2X-Y) = 4-3 = 1$.

$V(X+Y) = 1+4 = 5$، $\sigma(X+Y) = \sqrt{5}$.

$V(2X-Y) = 4V(X)+V(Y) = 4+4 = 8$.
:::

## 🎯 خلاصة

$$\boxed{A,B \text{ مستقلتان} \iff P(A \cap B) = P(A)P(B)}$$

- الاستقلالية ≠ التنافي.
- تجارب مستقلة: جداء الاحتمالات.
- $V(X+Y) = V(X) + V(Y)$ إذا $X, Y$ مستقلان.
