# درس: قانون برنولي وقانون ثنائي الحد

## 1. تجربة برنولي

::: definition
**تعريف 1.1**

تجربة ذات نتيجتين فقط: **نجاح** ($p$) أو **فشل** ($q = 1-p$).

المتغير المرتبط: $X \in \{0, 1\}$، $P(X=1) = p$، $P(X=0) = q$.
:::

::: property
**⭐ خواص**

$E(X) = p$، $V(X) = pq$، $\sigma(X) = \sqrt{pq}$.

**الدليل:**
$E(X) = 0 \cdot q + 1 \cdot p = p$.
$E(X^2) = 0^2 q + 1^2 p = p$.
$V(X) = p - p^2 = p(1-p) = pq$.
:::

## 2. قانون ثنائي الحد $\mathcal{B}(n,p)$

::: definition
**تعريف 2.1**

نكرّر $n$ مرة تجربة برنولي (باستقلالية). $X$ = عدد النجاحات:

$$\boxed{P(X=k) = C_n^k\, p^k\, q^{n-k}, \quad k \in \{0,1,\ldots,n\}}$$

نكتب: $X \sim \mathcal{B}(n, p)$.
:::

::: property
**⭐ الخواص الأساسية**

$$\boxed{E(X) = np} \qquad \boxed{V(X) = npq} \qquad \boxed{\sigma(X) = \sqrt{npq}}$$

**التحقق:** $E(X) = \sum_{k=0}^n k C_n^k p^k q^{n-k} = np$ (بعلاقة $kC_n^k = nC_{n-1}^{k-1}$).
:::

## 3. شروط تطبيق $\mathcal{B}(n,p)$

::: remark
**⭐ يجب التحقق من:**

1. $n$ تجربة برنولي واضحة (نجاح/فشل).
2. التجارب **مستقلة**.
3. $p$ **ثابت** عبر جميع التجارب.

**مثال يطبق:** رمي حجر نرد $n$ مرة، $X$ = عدد مرات ظهور $6$.

**مثال لا يطبق:** سحب بلا إعادة (لأن $p$ يتغيّر).
:::

## 4. أمثلة حساب الاحتمالات

::: example
**📝 المثال 1:** $X \sim \mathcal{B}(10, 0{,}3)$.

$P(X=3) = C_{10}^3 (0{,}3)^3 (0{,}7)^7 = 120 \times 0{,}027 \times 0{,}0824 \approx 0{,}267$.

$E(X) = 10 \times 0{,}3 = 3$، $\sigma(X) = \sqrt{10 \times 0{,}3 \times 0{,}7} = \sqrt{2{,}1} \approx 1{,}45$.
:::

::: example
**📝 المثال 2:** قصّاب: $80\%$ طازجة. نختار $5$.

$X \sim \mathcal{B}(5, 0{,}8)$.

$P(X=5) = (0{,}8)^5 \approx 0{,}328$.

$P(X \geq 4) = P(X=4)+P(X=5) = C_5^4(0{,}8)^4(0{,}2) + (0{,}8)^5$

$= 5 \times 0{,}4096 \times 0{,}2 + 0{,}3277 = 0{,}4096 + 0{,}3277 = 0{,}7373$.
:::

::: example
**📝 المثال 3 (العكسي):** $X \sim \mathcal{B}(n, 0{,}5)$. أوجد $n$ بحيث $P(X \geq 1) \geq 0{,}99$.

$P(X \geq 1) = 1 - P(X=0) = 1 - (0{,}5)^n \geq 0{,}99$

$(0{,}5)^n \leq 0{,}01 \Rightarrow n\ln(0{,}5) \leq \ln(0{,}01) \Rightarrow n \geq \dfrac{\ln(0{,}01)}{\ln(0{,}5)} = \dfrac{-4{,}605}{-0{,}693} \approx 6{,}64$

إذن $n \geq 7$.
:::

## 5. الاحتمالات التراكمية

::: example
**📝 المثال 4:** $X \sim \mathcal{B}(20, 0{,}3)$.

$P(X \leq 2) = P(0)+P(1)+P(2)$

$= (0{,}7)^{20} + 20(0{,}3)(0{,}7)^{19} + C_{20}^2(0{,}3)^2(0{,}7)^{18}$

$\approx 0{,}0008 + 0{,}0068 + 0{,}0278 = 0{,}0354$.

$P(4 \leq X \leq 7) = P(X \leq 7) - P(X \leq 3)$ (بالجداول أو الحاسبة).
:::

