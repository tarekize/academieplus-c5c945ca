# درس: القيمة المتوسطة

القيمة المتوسطة لدالة تُعبّر عن "المستوى المتوسط" الذي تحتله الدالة على مجال ما.

## 1. التعريف

::: definition
**تعريف 1.1 — القيمة المتوسطة**

ليكن $f$ دالة مستمرة على $[a\,;\,b]$ ($a < b$). **القيمة المتوسطة** لـ $f$ على $[a\,;\,b]$:

$$\boxed{\mu = \frac{1}{b-a}\int_a^b f(x)\,dx}$$

**وحدة القياس:** نفس وحدة $f$ (على عكس التكامل الذي لوحدته مربّعة).
:::

::: graphic
**📈 التفسير الهندسي**

القيمة المتوسطة $\mu$ هي ارتفاع المستطيل الذي:
- قاعدته = $b - a$
- مساحته = $\int_a^b f(x)\,dx$ (مساحة الحقل تحت المنحنى)

بعبارة أخرى: $\mu \times (b-a) = \int_a^b f(x)\,dx$
:::

## 2. مبرهنة القيمة المتوسطة

::: property
**⭐ مبرهنة 2.1 — الوجود**

إذا كانت $f$ مستمرة على $[a\,;\,b]$، يوجد $c \in [a\,;\,b]$ بحيث:
$$f(c) = \mu = \frac{1}{b-a}\int_a^b f(x)\,dx$$

**التفسير:** المنحنى يبلغ قيمته المتوسطة في نقطة على الأقل.
:::

## 3. أمثلة تطبيقية

::: example
**📝 المثال 1:** القيمة المتوسطة لـ $f(x) = x^2$ على $[0\,;\,3]$.

$$\mu = \frac{1}{3}\int_0^3 x^2\,dx = \frac{1}{3}\left[\frac{x^3}{3}\right]_0^3 = \frac{1}{3} \times 9 = 3$$

**التحقق من المبرهنة:** يوجد $c$ بحيث $f(c) = c^2 = 3$، أي $c = \sqrt{3} \in [0\,;\,3]$ ✓.
:::

::: example
**📝 المثال 2:** القيمة المتوسطة لـ $f(x) = e^x$ على $[0\,;\,2]$.

$$\mu = \frac{1}{2}\int_0^2 e^x\,dx = \frac{1}{2}[e^x]_0^2 = \frac{e^2-1}{2} \approx 3{,}19$$
:::

::: example
**📝 المثال 3:** القيمة المتوسطة لـ $f(x) = \sin x$ على $[0\,;\,\pi]$.

$$\mu = \frac{1}{\pi}\int_0^\pi \sin x\,dx = \frac{1}{\pi}[-\cos x]_0^\pi = \frac{1}{\pi}(1+1) = \frac{2}{\pi} \approx 0{,}637$$
:::

::: example
**📝 المثال 4 — متوسط السرعة:**

سرعة جسيم: $v(t) = 3t^2 - 2t + 1$ (م/ث) خلال $[0\,;\,2]$.

$$\bar{v} = \frac{1}{2}\int_0^2 (3t^2-2t+1)\,dt = \frac{1}{2}[t^3-t^2+t]_0^2 = \frac{1}{2}(8-4+2) = 3 \text{ م/ث}$$

**ملاحظة:** المسافة المقطوعة = $\bar{v} \times \Delta t = 3 \times 2 = 6$ م.
:::

::: example
**📝 المثال 5 — الطاقة الكهربائية المتوسطة:**

قوة كهربائية لحظية: $P(t) = P_0 \cos^2(\omega t)$ وات.

**نتيجة معروفة:** $\bar{P} = \dfrac{P_0}{2}$ (متوسط $\cos^2$ على دور كامل = $1/2$).

$$\bar{P} = \frac{1}{T}\int_0^T P_0\cos^2(\omega t)\,dt = \frac{P_0}{2}$$
:::

::: example
**📝 المثال 6 — متوسط درجة الحرارة:**

$T(t) = 20 + 5\cos\!\left(\dfrac{\pi t}{12}\right)$ (بالساعات) على $[0\,;\,12]$.

$$\bar{T} = \frac{1}{12}\int_0^{12}\left(20 + 5\cos\frac{\pi t}{12}\right)dt$$
$$= \frac{1}{12}\left[20t + \frac{60}{\pi}\sin\frac{\pi t}{12}\right]_0^{12}$$
$$= \frac{1}{12}\left(240 + \frac{60}{\pi}\sin\pi - 0\right) = \frac{240}{12} = 20°C$$

**تفسير:** متوسط درجة الحرارة مساوٍ للحرارة الوسطى $20°C$.
:::

## 4. تطبيق إحصائي

::: remark
**🔑 الصلة بالإحصاء:**

للمتغير العشوائي المستمر $X$ ذي الكثافة $f$:
$$E(X) = \int_{-\infty}^{+\infty} x\,f(x)\,dx = \text{القيمة المتوسطة المرجّحة}$$

هذا يُعمّم مفهوم المتوسط الحسابي إلى الحالة المستمرة.
:::

## 🎯 خلاصة

$$\boxed{\mu = \frac{1}{b-a}\int_a^b f(x)\,dx}$$

- هندسياً: مساحة المستطيل ذو الارتفاع $\mu$ = مساحة الحقل تحت المنحنى.
- يوجد دائماً $c \in [a\,;\,b]$ بحيث $f(c) = \mu$.
- تطبيقات: متوسط السرعة، متوسط الحرارة، الطاقة الكهربائية.
