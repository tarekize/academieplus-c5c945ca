# درس: الدوال الأصلية

**الدالة الأصلية** هي مفهوم أساسي في التحليل الرياضي، يُمثّل عملية عكس الاشتقاق. وهي الخطوة التمهيدية نحو الحساب التكاملي.

## 1. التعريف والمفهوم

::: definition
**تعريف 1.1 — الدالة الأصلية**

ليكن $f$ دالة معرّفة على مجال $I$. تُسمى الدالة $F$ **دالةً أصليةً** (أو **دالة مكافئة**) لـ $f$ على $I$ إذا كانت:

$$\forall x \in I,\quad F'(x) = f(x)$$

بعبارة أخرى: $F$ هي الدالة التي إذا اشتققناها حصلنا على $f$.

$$\text{الاشتقاق: } F \xrightarrow{\text{اشتقاق}} f \qquad \text{الأصلية: } f \xrightarrow{\text{تكامل}} F$$
:::

::: example
**📝 أمثلة أساسية:**

| $f(x)$ | $F(x)$ (دالة أصلية) | التحقق: $F'(x)$ |
|---|---|---|
| $2x$ | $x^2$ | $(x^2)' = 2x$ ✓ |
| $x^3$ | $x^4/4$ | $(x^4/4)' = x^3$ ✓ |
| $e^x$ | $e^x$ | $(e^x)' = e^x$ ✓ |
| $\cos x$ | $\sin x$ | $(\sin x)' = \cos x$ ✓ |
| $\sin x$ | $-\cos x$ | $(-\cos x)' = \sin x$ ✓ |
| $1/x$ | $\ln\|x\|$ | $(\ln\|x\|)' = 1/x$ ✓ |
:::

## 2. عدم وحدانية الدالة الأصلية

::: property
**⭐ مبرهنة 2.1 — الأصليات متعددة**

إذا كانت $F$ دالة أصلية لـ $f$ على $I$، فإن **كل الدوال الأصلية** لـ $f$ على $I$ هي من الشكل:
$$G(x) = F(x) + c \quad \text{حيث } c \in \mathbb{R}$$

**البرهان:**
- إذا $G = F + c$: $G'(x) = F'(x) + 0 = f(x)$ ✓
- إذا $G' = F' = f$: $(G-F)' = 0 \Rightarrow G - F = c$ (ثابت) ✓
:::

::: example
**📝 تطبيق:** الدوال الأصلية لـ $f(x) = 2x$ هي جميع الدوال:
$$F_c(x) = x^2 + c \quad (c \in \mathbb{R})$$

- $F_0(x) = x^2$ ✓
- $F_3(x) = x^2 + 3$ ✓
- $F_{-7}(x) = x^2 - 7$ ✓

**هندسياً:** كل منحنيات هذه الدوال متوازية (تُستنتج إحداها من الأخرى بإزاحة عمودية).
:::

## 3. وجود الدوال الأصلية

::: property
**⭐ مبرهنة الوجود (بدون برهان)**

كل دالة **مستمرة** على مجال $I$ تمتلك دالة أصلية على $I$.

**تنبيه:** الدوال غير المستمرة قد لا تمتلك دالة أصلية.
:::

## 4. جدول الدوال الأصلية الأساسية

::: property
**⭐ الجدول الموسّع**

| الدالة $f(x)$ | دالة أصلية $F(x)$ | الشرط |
|---|---|---|
| $k$ (ثابت) | $kx$ | $k \in \mathbb{R}$ |
| $x^n$، $n \neq -1$ | $\dfrac{x^{n+1}}{n+1}$ | $n \in \mathbb{Z}$ |
| $\sqrt{x}$ | $\dfrac{2}{3}x^{3/2}$ | $x > 0$ |
| $x^\alpha$، $\alpha \neq -1$ | $\dfrac{x^{\alpha+1}}{\alpha+1}$ | $x > 0$، $\alpha \in \mathbb{R}$ |
| $\dfrac{1}{x}$ | $\ln\|x\|$ | $x \neq 0$ |
| $e^x$ | $e^x$ | |
| $e^{ax}$ | $\dfrac{e^{ax}}{a}$ | $a \neq 0$ |
| $\cos x$ | $\sin x$ | |
| $\sin x$ | $-\cos x$ | |
| $\dfrac{1}{\cos^2 x}$ | $\tan x$ | $\cos x \neq 0$ |
:::

## 5. تحديد الدالة الأصلية بشرط أولي

::: property
**⭐ وحدانية الأصلية بشرط**

لكل $a \in I$ و $b \in \mathbb{R}$، توجد **دالة أصلية وحيدة** $F$ لـ $f$ على $I$ تحقق:
$$F(a) = b$$

**الطريقة:**
1. أوجد الصيغة العامة للأصلية: $F(x) = G(x) + c$.
2. طبّق الشرط: $G(a) + c = b$.
3. استنتج $c$.
:::

::: example
**📝 المثال 1:** أوجد الدالة الأصلية $F$ لـ $f(x) = 3x^2 + 2x - 1$ التي تحقق $F(1) = 4$.

**✅ الحل:**

الصيغة العامة: $F(x) = x^3 + x^2 - x + c$.

$F(1) = 1 + 1 - 1 + c = 1 + c = 4 \Rightarrow c = 3$.

$$\boxed{F(x) = x^3 + x^2 - x + 3}$$

**تحقق:** $F'(x) = 3x^2 + 2x - 1 = f(x)$ ✓ و $F(1) = 1 + 1 - 1 + 3 = 4$ ✓
:::

::: example
**📝 المثال 2:** أوجد $F$ الأصلية لـ $f(x) = e^x + \cos x$ مع $F(0) = 2$.

$F(x) = e^x + \sin x + c$.

$F(0) = 1 + 0 + c = 2 \Rightarrow c = 1$.

$$\boxed{F(x) = e^x + \sin x + 1}$$
:::

::: example
**📝 المثال 3 (تكميلي):** أوجد $F$ الأصلية لـ $f(x) = \sqrt{x} + \dfrac{1}{x^2}$ على $]0\,;\,+\infty[$ مع $F(1) = 0$.

$F(x) = \dfrac{2}{3}x^{3/2} + \dfrac{x^{-1}}{-1} + c = \dfrac{2}{3}x\sqrt{x} - \dfrac{1}{x} + c$.

$F(1) = \dfrac{2}{3} - 1 + c = 0 \Rightarrow c = \dfrac{1}{3}$.

$$\boxed{F(x) = \frac{2}{3}x\sqrt{x} - \frac{1}{x} + \frac{1}{3}}$$
:::

## 6. التفسير الهندسي والفيزيائي

::: graphic
**📈 التفسير الهندسي**

مجموعة الدوال الأصلية لدالة $f$ تُمثّل **عائلة من المنحنيات المتوازية**.

كل منحنى يُستنتج من الآخر بإزاحة عمودية بمقدار $c$.

الشرط الأولي $F(a) = b$ يُحدّد **منحنى وحيداً** من هذه العائلة.
:::

::: remark
**🔬 التفسير الفيزيائي:**

- إذا كان $f(t)$ سرعة جسم، فإن الدالة الأصلية $F(t)$ هي موضع الجسم.
- إذا كان $f(t)$ التسارع، فإن الدالة الأصلية هي السرعة.

الشرط الأولي يُعطي الموضع (أو السرعة) عند اللحظة $t = a$.
:::

## 🎯 خلاصة

$$\boxed{F \text{ أصلية لـ } f \iff F' = f}$$

- الأصليات تختلف بثابت: $\{F + c \;|\; c \in \mathbb{R}\}$.
- كل دالة مستمرة تمتلك أصلية.
- الشرط الأولي $F(a) = b$ يُعيّن الأصلية بشكل وحيد.
