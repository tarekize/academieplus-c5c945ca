# درس: بعض تطبيقات الحساب التكاملي

نوظّف التكامل لحساب مساحات، حجوم أجسام دوران، وتطبيقات فيزيائية متنوعة.

## 1. حساب المساحات (مراجعة وتعمّق)

::: property
**⭐ ملخص المساحات**

- بين $f$ والمحور: $\int_a^b |f(x)|\,dx$
- بين $f$ و$g$: $\int_a^b |f(x)-g(x)|\,dx$
- المساحة الجبرية: $\int_a^b f(x)\,dx$
:::

::: example
**📝 المثال 1:** مساحة بين $f(x) = \ln x$ و $g(x) = \ln^2 x$ على $[1\,;\,e]$.

**الإشارة على $[1\,;\,e]$:** $0 \leq \ln x \leq 1$، إذن $\ln^2 x \leq \ln x$.

$\mathcal{A} = \int_1^e (\ln x - \ln^2 x)\,dx = \underbrace{\int_1^e \ln x\,dx}_{=1} - \int_1^e \ln^2 x\,dx$.

$\int_1^e \ln^2 x\,dx$ بالتجزئة ($u = \ln^2 x$، $v' = 1$):
$= [x\ln^2 x]_1^e - \int_1^e 2\ln x\,dx = e - 2 \times 1 = e-2$.

$$\mathcal{A} = 1 - (e-2) = 3-e \approx 0{,}28 \text{ وحدة مربعة}$$
:::

## 2. حجم جسم الدوران

::: property
**⭐ حجم الدوران حول محور الفواصل**

الحجم الناتج عن دوران المنطقة بين منحنى $f$ ومحور الفواصل على $[a\,;\,b]$:

$$\boxed{V = \pi \int_a^b [f(x)]^2\,dx}$$

**مصدر الصيغة:** نقطّع المجسم إلى أقراص رقيقة، كل قرص حجمه $\pi [f(x)]^2\,dx$.
:::

::: example
**📝 المثال 2 — الأسطوانة:**

$f(x) = r$ (ثابت) على $[0\,;\,h]$:
$$V = \pi\int_0^h r^2\,dx = \pi r^2 h \quad \checkmark$$
:::

::: example
**📝 المثال 3 — المخروط:**

$f(x) = x$ على $[0\,;\,h]$:
$$V = \pi\int_0^h x^2\,dx = \pi\left[\frac{x^3}{3}\right]_0^h = \frac{\pi h^3}{3} = \frac{1}{3}\pi r^2 h \quad (r = h) \checkmark$$
:::

::: example
**📝 المثال 4 — الكرة:**

دوران نصف الدائرة $f(x) = \sqrt{R^2-x^2}$ على $[-R\,;\,R]$:

$$V = \pi\int_{-R}^{R}(R^2-x^2)\,dx = \pi\left[R^2x - \frac{x^3}{3}\right]_{-R}^{R} = \pi \cdot \frac{4R^3}{3} = \frac{4\pi R^3}{3} \quad \checkmark$$
:::

::: example
**📝 المثال 5 (تكميلي):** الحجم الناتج عن دوران منطقة بين $y = e^x$ ومحور الفواصل على $[0\,;\,1]$.

$$V = \pi\int_0^1 e^{2x}\,dx = \pi\left[\frac{e^{2x}}{2}\right]_0^1 = \frac{\pi(e^2-1)}{2} \approx 10{,}04$$
:::

## 3. تطبيقات فيزيائية

::: example
**📝 المثال 6 — المسافة المقطوعة:**

$v(t) = t^2 - 4$ (م/ث) على $[0\,;\,3]$.

**النزوح الجبري:** $\int_0^3 (t^2-4)\,dt = [t^3/3-4t]_0^3 = 9-12 = -3$ م.

**المسافة الحقيقية:** $v(t) = 0 \iff t = 2$.

$\left|\int_0^2 (t^2-4)\,dt\right| + \int_2^3 (t^2-4)\,dt$
$= |8/3-8| + (9-12)-(8/3-8) = 16/3 + (-3+16/3) = 16/3 + 7/3 = 23/3 \approx 7{,}67$ م.
:::

::: example
**📝 المثال 7 — شغل القوة:**

قوة $F(x) = 3x^2 + 2$ (نيوتن) على مسافة $x$ من $0$ إلى $4$ م.

$$W = \int_0^4 F(x)\,dx = \int_0^4 (3x^2+2)\,dx = [x^3+2x]_0^4 = 64+8 = 72 \text{ جول}$$
:::

## 4. التكامل والاحتمالات

::: example
**📝 المثال 8:** $f(x) = 2e^{-2x}$ على $[0\,;\,+\infty[$. احسب $P(1 \leq X \leq 2)$ و $P(X > 3)$.

$$P(1 \leq X \leq 2) = \int_1^2 2e^{-2x}\,dx = [-e^{-2x}]_1^2 = e^{-2}-e^{-4} \approx 0{,}117$$

$$P(X > 3) = \int_3^{+\infty} 2e^{-2x}\,dx = \lim_{b\to\infty}[-e^{-2x}]_3^b = e^{-6} \approx 0{,}0025$$
:::

## 🎯 خلاصة

- المساحة: $\int|f|\,dx$ أو $\int|f-g|\,dx$.
- حجم الدوران: $V = \pi\int[f(x)]^2\,dx$.
- الفيزياء: المسافة = $\int|v|\,dt$، الشغل = $\int F\,dx$.
- الاحتمالات: $P(a \leq X \leq b) = \int_a^b f(x)\,dx$.
