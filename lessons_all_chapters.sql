# درس: قياس تلاؤم سلسلة مشاهدة ونموذج احتمالي

نختبر توافق بيانات تجريبية مع نموذج احتمالي نظري.

## 1. الإشكالية

::: remark
**🎯 السؤال:** لدينا بيانات مشاهدة ($n$ تجربة). هل تنبثق من توزيع احتمالي محدد؟

**الطريقة:** نقارن التردد المشاهد بالتردد النظري المتوقع.
:::

## 2. التردد المشاهد مقابل النظري

::: definition
**التعريفات:**

- **التردد المطلق المشاهد** $O_i$: عدد مرات ظهور القيمة $x_i$.
- **التردد النظري** $E_i = n \times P(X = x_i)$: العدد المتوقع.
- **التردد النسبي** $f_i = O_i/n$: يُقدّر الاحتمال.

**الشرط:** $E_i \geq 5$ لكل فئة (شرط تقني للاختبار).
:::

## 3. إحصاء $\chi^2$ (شي-تربيع)

::: definition
**الإحصاء:**

$$\boxed{\chi^2 = \sum_{i=1}^k \frac{(O_i - E_i)^2}{E_i}}$$

- **صغير:** التلاؤم جيد.
- **كبير:** البيانات لا تتوافق مع النموذج.

**عدد درجات الحرية:** $\nu = k - 1$ (أو $k-p-1$ إذا قدّرنا $p$ معاملاً من البيانات).
:::

::: example
**📝 المثال 1 — التوزيع المنتظم:**

حجر نرد رُمي $120$ مرة:

| الوجه | $O_i$ | $E_i = 20$ | $(O_i-E_i)^2/E_i$ |
|---|---|---|---|
| 1 | 18 | 20 | $0{,}2$ |
| 2 | 22 | 20 | $0{,}2$ |
| 3 | 19 | 20 | $0{,}05$ |
| 4 | 21 | 20 | $0{,}05$ |
| 5 | 17 | 20 | $0{,}45$ |
| 6 | 23 | 20 | $0{,}45$ |

$\chi^2 = 1{,}4$، $\nu = 5$، قيمة جدولية ($5\%$) = $11{,}07$.

**القرار:** $1{,}4 < 11{,}07$ → لا نرفض التوازن.
:::

::: example
**📝 المثال 2 — التلاؤم مع ثنائي الحد:**

$200$ عائلة ذات $3$ أطفال. النموذج: $X \sim \mathcal{B}(3, 0{,}5)$.

| $k$ | $O_k$ | $P(X=k)$ | $E_k$ |
|---|---|---|---|
| 0 | 22 | $0{,}125$ | 25 |
| 1 | 78 | $0{,}375$ | 75 |
| 2 | 80 | $0{,}375$ | 75 |
| 3 | 20 | $0{,}125$ | 25 |

$\chi^2 = \dfrac{9}{25} + \dfrac{9}{75} + \dfrac{25}{75} + \dfrac{25}{25} = 0{,}36 + 0{,}12 + 0{,}33 + 1 = 1{,}81$.

$\nu = 3$. قيمة جدولية = $7{,}81$. **لا نرفض النموذج**.
:::

## 4. الفترة الثقة لنسبة (تطبيق)

::: property
**⭐ فترة الثقة**

إذا لاحظنا $k$ نجاحاً في $n$ تجربة، فإن $\hat{p} = k/n$ يُقدّر $p$، وفترة الثقة بمستوى $95\%$:

$$\hat{p} - 1{,}96\sqrt{\frac{\hat{p}(1-\hat{p})}{n}} \leq p \leq \hat{p} + 1{,}96\sqrt{\frac{\hat{p}(1-\hat{p})}{n}}$$
:::

::: example
**📝 المثال 3:** في عينة من $400$ شخص، $120$ يؤيدون قانوناً ($\hat{p} = 0{,}3$).

$$\text{هامش الخطأ} = 1{,}96\sqrt{\frac{0{,}3 \times 0{,}7}{400}} = 1{,}96 \times 0{,}023 \approx 0{,}045$$

فترة الثقة: $[0{,}255\,;\,0{,}345]$ أي بين $25{,}5\%$ و $34{,}5\%$.
:::

## 🎯 خلاصة

- $\chi^2 = \sum(O_i-E_i)^2/E_i$ يقيس الانحراف عن النموذج.
- إذا $\chi^2 <$ قيمة الجدول: التلاؤم مقبول.
- يجب $E_i \geq 5$ لكل فئة.
