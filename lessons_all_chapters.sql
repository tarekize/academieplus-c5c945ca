# درس: دراسة الدوال $x \mapsto a^x$ و $x \mapsto \sqrt[n]{x}$

ندرس في هذا الدرس دالتين أساسيتين: **الدالة الأسية بأساس $a$** ودالة **الجذر الرتبة $n$**.

## 1. الدالة $f(x) = a^x$ (الأساس $a > 0$، $a \neq 1$)

::: definition
**تعريف 1.1**

لكل $a > 0$، $a \neq 1$، نعرّف على $\mathbb{R}$:
$$f(x) = a^x = e^{x \ln a}$$

الحالة $a = 1$ مستثناة لأن $1^x = 1$ ثابتة وغير مثيرة للاهتمام.
:::

::: property
**⭐ خواص الدالة $a^x$**

| الخاصية | $a > 1$ | $0 < a < 1$ |
|---|---|---|
| المجال | $\mathbb{R}$ | $\mathbb{R}$ |
| الصورة | $]0\,;\,+\infty[$ | $]0\,;\,+\infty[$ |
| المشتقة | $\ln(a) \cdot a^x > 0$ | $\ln(a) \cdot a^x < 0$ |
| الاتجاه | متزايدة تماماً | متناقصة تماماً |
| عند $0$ | $f(0) = 1$ | $f(0) = 1$ |
| مقارب | $y = 0$ عند $-\infty$ | $y = 0$ عند $+\infty$ |
:::

::: property
**⭐ النهايات التفصيلية**

**حالة $a > 1$:**
$$\lim_{x \to +\infty} a^x = +\infty \qquad \lim_{x \to -\infty} a^x = 0^+$$

**حالة $0 < a < 1$:**
$$\lim_{x \to +\infty} a^x = 0^+ \qquad \lim_{x \to -\infty} a^x = +\infty$$

**ملاحظة:** جميع منحنيات $a^x$ تشترك في النقطة $(0, 1)$ لأن $a^0 = 1$.
:::

## 2. الجدول التغيراتي والدراسة الكاملة

::: example
**📝 دراسة كاملة لـ $f(x) = 2^x$:**

**المشتقة:** $f'(x) = \ln 2 \cdot 2^x$. بما أن $\ln 2 > 0$ و $2^x > 0$: $f'(x) > 0$.

**جدول التغيرات:**

| $x$ | $-\infty$ | | $-1$ | | $0$ | | $1$ | | $+\infty$ |
|---|---|---|---|---|---|---|---|---|---|
| $f'(x)$ | | $+$ | | $+$ | | $+$ | | $+$ | |
| $f(x)$ | $0^+$ | $\nearrow$ | $1/2$ | $\nearrow$ | $1$ | $\nearrow$ | $2$ | $\nearrow$ | $+\infty$ |

**القيم الخاصة المفيدة:**

| $x$ | $-3$ | $-2$ | $-1$ | $0$ | $1$ | $2$ | $3$ |
|---|---|---|---|---|---|---|---|
| $2^x$ | $1/8$ | $1/4$ | $1/2$ | $1$ | $2$ | $4$ | $8$ |
:::

::: example
**📝 دراسة $g(x) = \left(\dfrac{1}{3}\right)^x = 3^{-x}$:**

**المشتقة:** $g'(x) = \ln(1/3) \cdot (1/3)^x = -\ln 3 \cdot (1/3)^x < 0$ ($g$ متناقصة).

| $x$ | $-\infty$ | | $0$ | | $+\infty$ |
|---|---|---|---|---|---|
| $g'(x)$ | | $-$ | | $-$ | |
| $g(x)$ | $+\infty$ | $\searrow$ | $1$ | $\searrow$ | $0^+$ |

**ملاحظة:** $(1/3)^x = 1/3^x = 3^{-x}$، أي منحنى $g$ هو انعكاس منحنى $3^x$ بالنسبة لمحور التراتيب.
:::

## 3. العلاقة بين $a^x$ و $e^x$

::: property
**⭐ التحويل الأساسي**

$$a^x = e^{x \ln a}$$

هذا يعني أن دراسة أي دالة أسية ترجع إلى دراسة $e^x$ بتغيير المتغير.

**مثال:** $f(x) = 5^{2x-1}$

$$f(x) = e^{(2x-1)\ln 5} \Rightarrow f'(x) = 2\ln 5 \cdot e^{(2x-1)\ln 5} = 2\ln 5 \cdot 5^{2x-1}$$
:::

## 4. الدالة $g(x) = \sqrt[n]{x} = x^{1/n}$

::: property
**⭐ خواص مفصّلة**

**حالة $n$ فردي ($n = 1, 3, 5, \ldots$):**
- $g$ معرّفة على $\mathbb{R}$، دالة فردية.
- $g(-x) = -g(x)$.
- على $]0\,;\,+\infty[$: $g'(x) = \frac{1}{n}x^{1/n-1} > 0$ (متزايدة).

**حالة $n$ زوجي ($n = 2, 4, 6, \ldots$):**
- $g$ معرّفة على $[0\,;\,+\infty[$، دالة شفعية على مجالها.
- $g(0) = 0$، $g(1) = 1$.
- $g'(x) = \frac{1}{n}x^{1/n-1} > 0$ (متزايدة على $]0\,;\,+\infty[$).
- $\lim_{x \to 0^+} g'(x) = +\infty$ (مماس قائم في الأصل).
:::

::: example
**📝 دراسة $h(x) = \sqrt[3]{x} = x^{1/3}$:**

- مجال التعريف: $\mathbb{R}$.
- $h'(x) = \frac{1}{3}x^{-2/3} = \frac{1}{3\sqrt[3]{x^2}} > 0$ (متزايدة على $\mathbb{R} \setminus \{0\}$).
- $\lim_{x\to 0} h'(x) = +\infty$ (مماس قائم في الأصل).
- دالة فردية: منحنى متناظر بالنسبة للأصل.

| $x$ | $-\infty$ | | $0$ | | $+\infty$ |
|---|---|---|---|---|---|
| $h(x)$ | $-\infty$ | $\nearrow$ | $0$ | $\nearrow$ | $+\infty$ |
:::

## 5. علاقة الدوال العكسية

::: property
**⭐ المبدأ العام**

على $[0\,;\,+\infty[$، كل من $x \mapsto x^n$ ($n \in \mathbb{N}^*, n \geq 2$) و $x \mapsto x^{1/n}$ دالتان عكسيتان.

أي: $(\sqrt[n]{x})^n = x$ و $\sqrt[n]{x^n} = x$ لكل $x \geq 0$.

**هندسياً:** منحنياهما متماثلان بالنسبة للمستقيم $y = x$.
:::

## 6. معادلات ومتراجحات

::: example
**📝 المثال 3:** حلّ $3^x \geq 9^{x-1}$.

$$9^{x-1} = (3^2)^{x-1} = 3^{2x-2}$$

بما أن $x \mapsto 3^x$ متزايدة:
$$3^x \geq 3^{2x-2} \iff x \geq 2x-2 \iff x \leq 2$$

$$S = ]-\infty\,;\,2]$$
:::

::: example
**📝 المثال 4:** حلّ $4^x - 2^{x+1} + 1 = 0$.

نضع $X = 2^x > 0$: $\;4^x = (2^x)^2 = X^2$، $\;2^{x+1} = 2 \cdot 2^x = 2X$.

$$X^2 - 2X + 1 = 0 \iff (X-1)^2 = 0 \iff X = 1$$

$$2^x = 1 = 2^0 \iff x = 0 \qquad S = \{0\}$$
:::

::: example
**📝 المثال 5:** حلّ $\left(\dfrac{1}{2}\right)^x > \left(\dfrac{1}{4}\right)^{1-x}$.

$$\left(\frac{1}{4}\right)^{1-x} = \left(\frac{1}{2}\right)^{2(1-x)} = \left(\frac{1}{2}\right)^{2-2x}$$

بما أن $0 < \frac{1}{2} < 1$، الدالة متناقصة:
$$\left(\frac{1}{2}\right)^x > \left(\frac{1}{2}\right)^{2-2x} \iff x < 2-2x \iff 3x < 2 \iff x < \frac{2}{3}$$

$$S = \left]-\infty\,;\,\frac{2}{3}\right[$$
:::

## 🎯 خلاصة

$$\boxed{(a^x)' = \ln a \cdot a^x} \qquad \boxed{(\sqrt[n]{x})' = \frac{1}{n} x^{1/n - 1}}$$

- $a^x = e^{x\ln a}$: الربط بين الدالتين الأساسيتين.
- إذا $a > 1$: $a^x$ متزايدة؛ إذا $0 < a < 1$: متناقصة.
- $\sqrt[n]{x}$ عكس $x^n$ على $[0\,;\,+\infty[$، منحنياهما متماثلان بالنسبة لـ $y = x$.
