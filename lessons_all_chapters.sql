# درس: تكامل دالة

**التكامل المحدود** أداة رياضية قوية ترتبط بمفهوم المساحة. يُمثّل المساحة الجبرية بين منحنى دالة ومحور الفواصل.

## 1. التعريف

::: definition
**تعريف 1.1 — التكامل المحدود**

ليكن $f$ دالة مستمرة على $[a\,;\,b]$ و $F$ دالة أصلية لها. نُعرّف **تكامل $f$ من $a$ إلى $b$** بـ:

$$\boxed{\int_a^b f(x)\,dx = \Big[F(x)\Big]_a^b = F(b) - F(a)}$$

النتيجة عدد حقيقي لا يعتمد على الدالة الأصلية المختارة.
:::

::: remark
**📌 الرموز والمصطلحات:**

- $\int_a^b$: رمز التكامل (S ممدود → مجموع)، يُقرأ "تكامل من $a$ إلى $b$"
- $f(x)$: المتكاملة (الدالة المُكاملة)
- $dx$: رمز المتغير (يذكّرنا بسياق الحساب)
- $a$: حدّ التكامل الأدنى (الأسفل)
- $b$: حدّ التكامل الأعلى (الأعلى)
- $[F(x)]_a^b$ يُقرأ: "F بين a وb"

**لماذا النتيجة لا تعتمد على الأصلية المختارة؟**

إذا $G = F + c$: $G(b) - G(a) = (F(b)+c) - (F(a)+c) = F(b) - F(a)$ ✓
:::

## 2. حساب التكامل خطوة بخطوة

::: example
**📝 المثال 1:** احسب $\int_1^3 (2x + 1)\,dx$.

**✅ الحل:**

1. أوجد أصلية: $F(x) = x^2 + x$
2. طبّق التعريف:
$$\int_1^3 (2x+1)\,dx = [x^2 + x]_1^3 = (9+3) - (1+1) = 12 - 2 = 10$$
:::

::: example
**📝 المثال 2:** احسب $\int_0^1 e^{2x}\,dx$.

**✅ الحل:** أصلية $e^{2x}$ هي $\dfrac{e^{2x}}{2}$.

$$\int_0^1 e^{2x}\,dx = \left[\frac{e^{2x}}{2}\right]_0^1 = \frac{e^2}{2} - \frac{1}{2} = \frac{e^2-1}{2}$$
:::

::: example
**📝 المثال 3:** احسب $\int_1^e \dfrac{1}{x}\,dx$.

$$\int_1^e \frac{1}{x}\,dx = [\ln x]_1^e = \ln e - \ln 1 = 1 - 0 = 1$$
:::

::: example
**📝 المثال 4:** احسب $\int_0^{\pi/2} \cos x\,dx$ و $\int_0^{\pi} \sin x\,dx$.

$$\int_0^{\pi/2} \cos x\,dx = [\sin x]_0^{\pi/2} = \sin(\pi/2) - \sin(0) = 1 - 0 = 1$$

$$\int_0^{\pi} \sin x\,dx = [-\cos x]_0^{\pi} = -\cos\pi + \cos 0 = 1 + 1 = 2$$

**تفسير:** التكامل الثاني = مساحة نصف الدائرة الإيجابي لـ $\sin$.
:::

::: example
**📝 المثال 5:** احسب $\int_0^1 \dfrac{2x}{x^2+1}\,dx$.

**✅ الحل:** $u = x^2+1$، $u' = 2x$، إذن أصلية $\dfrac{2x}{x^2+1}$ هي $\ln(x^2+1)$.

$$\int_0^1 \frac{2x}{x^2+1}\,dx = [\ln(x^2+1)]_0^1 = \ln 2 - \ln 1 = \ln 2$$
:::

## 3. التكامل والمساحة

::: property
**⭐ التفسير الهندسي الكامل**

إذا كانت $f$ **موجبة** على $[a\,;\,b]$:
$$\int_a^b f(x)\,dx = \mathcal{A}_{[a,b]}^+ \quad \text{(المساحة موجبة)}$$

إذا كانت $f$ **سالبة** على $[a\,;\,b]$:
$$\int_a^b f(x)\,dx = -\mathcal{A}_{[a,b]}^- \quad \text{(التكامل سالب)}$$

**المساحة الجبرية** = مجموع المساحات فوق المحور مطروحاً منها المساحات تحته.
:::

## 4. خاصية القيمة المتوسطة للاشتقاق

::: property
**⭐ مبرهنة القيمة المتوسطة للتكامل**

إذا $f$ مستمرة على $[a\,;\,b]$، يوجد $c \in [a\,;\,b]$ بحيث:
$$f(c) = \frac{1}{b-a}\int_a^b f(x)\,dx$$
:::

## 5. الدالة الأصلية بالتكامل

::: property
**⭐ تعريف الأصلية بالتكامل**

إذا $f$ مستمرة على $[a\,;\,b]$، الدالة:
$$F(x) = \int_a^x f(t)\,dt$$

هي الأصلية الوحيدة لـ $f$ التي تلغي عند $x = a$ (أي $F(a) = 0$).
:::

::: example
**📝 المثال 6 (الربط بالأصلية):** احسب $\int_0^2 (3x^2 - 2x + 4)\,dx$.

$$\int_0^2 (3x^2-2x+4)\,dx = [x^3-x^2+4x]_0^2 = (8-4+8) - 0 = 12$$
:::

## 🎯 خلاصة

$$\boxed{\int_a^b f(x)\,dx = F(b) - F(a)}$$

- النتيجة عدد حقيقي (المساحة الجبرية).
- $f \geq 0 \Rightarrow$ التكامل = المساحة الحقيقية.
- أوجد الأصلية → طبّق القيمة بين الحدين.
