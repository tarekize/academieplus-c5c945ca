# درس: الاحتمالات الشرطية

الاحتمال الشرطي يُعبّر عن احتمال حدوث حادثة **بمعلومية** حدوث أخرى.

## 1. التعريف والصيغة

::: definition
**تعريف 1.1**

ليكن $P(B) > 0$. الاحتمال الشرطي لـ $A$ علماً بوقوع $B$:

$$\boxed{P(A|B) = \frac{P(A \cap B)}{P(B)}}$$
:::

::: property
**⭐ صيغ متكافئة**

$$P(A \cap B) = P(B) \cdot P(A|B) = P(A) \cdot P(B|A)$$

**تذكير:** إذا $A \subset B$: $P(A|B) = P(A)/P(B)$.
:::

::: example
**📝 المثال 1 (بسيط):** في فصل من $30$ طالباً: $18$ نجحوا في الرياضيات ($R$)، $15$ في الفيزياء ($F$)، $10$ في كليهما.

$P(R|F) = \dfrac{P(R \cap F)}{P(F)} = \dfrac{10/30}{15/30} = \dfrac{10}{15} = \dfrac{2}{3}$.
:::

## 2. الشجرة الاحتمالية

::: graphic
**📈 بناء الشجرة:**

- **المستوى الأول:** الحوادث الأولية ($B_1, B_2, \ldots$) وعليها احتمالاتها $P(B_i)$.
- **المستوى الثاني:** الحادثة الهدف ($A$ أو $\bar{A}$) وعليها الاحتمالات الشرطية $P(A|B_i)$.
- **احتمال المسار:** جداء الاحتمالات على الفروع.
- **الاحتمال الكلي:** مجموع احتمالات جميع المسارات المؤدية إلى $A$.
:::

## 3. الاحتمال الكلي

::: property
**⭐ صيغة الاحتمال الكلي**

إذا كانت $(B_1, \ldots, B_n)$ تقسيماً لـ $\Omega$:

$$\boxed{P(A) = \sum_{i=1}^n P(B_i) \cdot P(A|B_i)}$$
:::

::: example
**📝 المثال 2 (ثلاثة مصانع):**

ثلاثة مصانع تنتج: $M_1$ نسبة $50\%$، $M_2$ نسبة $30\%$، $M_3$ نسبة $20\%$.

نسب المعيوب: $P(D|M_1) = 0{,}02$، $P(D|M_2) = 0{,}04$، $P(D|M_3) = 0{,}06$.

$$P(D) = 0{,}5 \times 0{,}02 + 0{,}3 \times 0{,}04 + 0{,}2 \times 0{,}06$$
$$= 0{,}010 + 0{,}012 + 0{,}012 = 0{,}034 = 3{,}4\%$$
:::

## 4. مبرهنة بايز

::: property
**⭐ صيغة بايز**

$$\boxed{P(B_i|A) = \frac{P(B_i) \cdot P(A|B_i)}{P(A)}}$$

**التطبيق:** من نتيجة (مشاهدة) نستنتج السبب الأرجح.
:::

::: example
**📝 المثال 3 (استكمال):** من المثال السابق، قطعة معيوبة من أيّ مصنع جاءت على الأرجح؟

$$P(M_1|D) = \frac{0{,}5 \times 0{,}02}{0{,}034} = \frac{0{,}01}{0{,}034} \approx 0{,}294$$

$$P(M_2|D) = \frac{0{,}3 \times 0{,}04}{0{,}034} = \frac{0{,}012}{0{,}034} \approx 0{,}353$$

$$P(M_3|D) = \frac{0{,}2 \times 0{,}06}{0{,}034} = \frac{0{,}012}{0{,}034} \approx 0{,}353$$

**الاستنتاج:** المصنع $M_2$ أو $M_3$ (الأرجح هو السبب).
:::

::: example
**📝 المثال 4 (تشخيص طبي):**

داء نادر نسبة إصابة $1\%$ ($P(M) = 0{,}01$). اختبار:
- $P(+|M) = 0{,}95$ (حساسية).
- $P(+|\bar{M}) = 0{,}05$ (معدل إيجابي كاذب).

إذا جاء الاختبار إيجابياً، ما احتمال الإصابة الفعلية؟

$$P(M|+) = \frac{0{,}01 \times 0{,}95}{0{,}01 \times 0{,}95 + 0{,}99 \times 0{,}05} = \frac{0{,}0095}{0{,}0590} \approx 16\%$$

**مفاجأة:** حتى مع نتيجة إيجابية، احتمال الإصابة $16\%$ فقط! (لأن المرض نادر).
:::

## 🎯 خلاصة

$$\boxed{P(A|B) = \frac{P(A\cap B)}{P(B)}} \qquad \boxed{P(A) = \sum_i P(B_i)P(A|B_i)} \qquad \boxed{P(B_i|A) = \frac{P(B_i)P(A|B_i)}{P(A)}}$$
