# درس: خواص التكامل

نستعرض الخواص الجبرية والهندسية للتكامل المحدود، التي تُمكّننا من حساب التكاملات بفعالية وإثبات متراجحات تتعلق بها.

## 1. الخواص الجبرية الأساسية

::: property
**⭐ الخطية**

لكل $\lambda, \mu \in \mathbb{R}$ و $f, g$ مستمرتان على $[a\,;\,b]$:
$$\int_a^b (\lambda f + \mu g)(x)\,dx = \lambda \int_a^b f(x)\,dx + \mu \int_a^b g(x)\,dx$$
:::

::: property
**⭐ التقاطع (الإضافية)**

لكل $a, b, c$ أعداد حقيقية (بأي ترتيب):
$$\int_a^c f(x)\,dx = \int_a^b f(x)\,dx + \int_b^c f(x)\,dx$$

**تفسير هندسي:** نقطة $b$ تُقسّم الفترة وتُضاف المساحتان.
:::

::: property
**⭐ قلب الحدود**

$$\int_a^b f(x)\,dx = -\int_b^a f(x)\,dx$$

**نتيجة:** $\int_a^a f(x)\,dx = 0$.
:::

## 2. خواص الترتيب

::: property
**⭐ الموجبية**

$$f(x) \geq 0 \text{ على } [a\,;\,b] \; (a<b) \Rightarrow \int_a^b f(x)\,dx \geq 0$$
:::

::: property
**⭐ المقارنة**

$$f(x) \leq g(x) \text{ على } [a\,;\,b] \Rightarrow \int_a^b f(x)\,dx \leq \int_a^b g(x)\,dx$$
:::

::: property
**⭐ المنحاة (خاصية القيمة المطلقة)**

$$\left|\int_a^b f(x)\,dx\right| \leq \int_a^b |f(x)|\,dx$$
:::

::: property
**⭐ تقدير التكامل**

إذا $m \leq f(x) \leq M$ على $[a\,;\,b]$:
$$m(b-a) \leq \int_a^b f(x)\,dx \leq M(b-a)$$

**تفسير:** التكامل محصور بين مستطيلين.
:::

## 3. أمثلة تطبيقية

::: example
**📝 المثال 1 (الخطية):**

$\int_0^3 f\,dx = 5$، $\int_0^3 g\,dx = -2$.

$$\int_0^3 (3f - 2g)\,dx = 3 \times 5 - 2 \times (-2) = 15 + 4 = 19$$
:::

::: example
**📝 المثال 2 (التقاطع):**

$\int_0^5 f\,dx = 7$، $\int_3^5 f\,dx = 2$.

$$\int_0^5 = \int_0^3 + \int_3^5 \Rightarrow \int_0^3 f\,dx = 7 - 2 = 5$$
:::

::: example
**📝 المثال 3 (تقدير):** بيّن أن $1 \leq \int_0^1 e^{x^2}\,dx \leq e$.

**✅ الحل:** على $[0\,;\,1]$: $0 \leq x^2 \leq 1$، إذن $e^0 \leq e^{x^2} \leq e^1$.

$$\int_0^1 1\,dx \leq \int_0^1 e^{x^2}\,dx \leq \int_0^1 e\,dx \Rightarrow 1 \leq \int_0^1 e^{x^2}\,dx \leq e \quad \checkmark$$
:::

::: example
**📝 المثال 4 (تقدير مُحكم):** بيّن أن $\dfrac{\pi}{4} \leq \int_0^{\pi/4} \dfrac{dx}{\cos x} \leq \dfrac{\pi\sqrt{2}}{4}$.

**✅ الحل:** على $[0\,;\,\pi/4]$: $\cos x \in [1/\sqrt{2}\,;\,1]$، إذن $\dfrac{1}{\cos x} \in [1\,;\,\sqrt{2}]$.

$$1 \leq \frac{1}{\cos x} \leq \sqrt{2} \Rightarrow \frac{\pi}{4} \leq \int_0^{\pi/4} \frac{dx}{\cos x} \leq \frac{\pi\sqrt{2}}{4} \quad \checkmark$$
:::

## 4. التكامل والتماثل

::: property
**⭐ الدالة الشفعية** ($f(-x) = f(x)$ على $[-a\,;\,a]$):
$$\int_{-a}^{a} f(x)\,dx = 2\int_0^a f(x)\,dx$$

**⭐ الدالة الفردية** ($f(-x) = -f(x)$ على $[-a\,;\,a]$):
$$\int_{-a}^{a} f(x)\,dx = 0$$
:::

::: example
**📝 المثال 5:**

$$\int_{-2}^{2} (x^3 + x^2 + 1)\,dx$$

- $x^3$ فردية: تكاملها = $0$.
- $x^2 + 1$ شفعية: $2\int_0^2 (x^2+1)\,dx = 2\left[\frac{x^3}{3}+x\right]_0^2 = 2\left(\frac{8}{3}+2\right) = 2 \times \frac{14}{3} = \frac{28}{3}$.

$$= 0 + \frac{28}{3} = \frac{28}{3}$$
:::

## 5. التكامل والدوريّة

::: property
**⭐ الدالة الدورية**

إذا كانت $f$ دالة دورية بالدور $T$ (أي $f(x+T) = f(x)$):

$$\int_a^{a+T} f(x)\,dx = \int_0^T f(x)\,dx \quad \text{لكل } a \in \mathbb{R}$$
:::

::: example
**📝 المثال 6:**

$$\int_\pi^{2\pi} \cos x\,dx = \int_0^\pi \cos x\,dx = [\sin x]_0^\pi = 0$$

(لأن $\cos$ دورية بالدور $2\pi$).
:::

## 🎯 خلاصة

- الخطية: $\int(\lambda f + \mu g) = \lambda\int f + \mu\int g$.
- التقاطع: $\int_a^c = \int_a^b + \int_b^c$.
- $f \geq 0 \Rightarrow \int_a^b f \geq 0$؛ $f \leq g \Rightarrow \int f \leq \int g$.
- دالة فردية على مجال متناسق: التكامل = $0$.
- تقدير: $m(b-a) \leq \int f \leq M(b-a)$.
