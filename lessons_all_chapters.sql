# درس: قوانين الاحتمالات المستمرة

## 1. المتغير العشوائي المستمر ودالة الكثافة

::: definition
**تعريف 1.1**

$f$ دالة كثافة احتمال إذا: $f \geq 0$ و $\int_{-\infty}^{+\infty} f(x)\,dx = 1$.

$$P(a \leq X \leq b) = \int_a^b f(x)\,dx$$

**ملاحظة:** $P(X = x_0) = 0$ دائماً → قيمة واحدة لا تحمل احتمالاً.
:::

::: definition
**دالة التوزيع التراكمي**

$$F(x) = P(X \leq x) = \int_{-\infty}^x f(t)\,dt$$

**الخواص:** $F$ متزايدة، $\lim_{x\to-\infty}F = 0$، $\lim_{x\to+\infty}F = 1$، $F' = f$.
:::

## 2. الأمل والتباين

::: definition
$$E(X) = \int_{-\infty}^{+\infty} x\,f(x)\,dx \qquad V(X) = \int_{-\infty}^{+\infty}(x-E(X))^2 f(x)\,dx$$
:::

## 3. القانون المنتظم $\mathcal{U}([a,b])$

::: definition
$$f(x) = \frac{1}{b-a} \text{ إذا } x \in [a\,;\,b], \quad 0 \text{ غيره}$$

$$E(X) = \frac{a+b}{2} \qquad V(X) = \frac{(b-a)^2}{12}$$
:::

::: example
**📝 الاحتمالات:**

$P(a \leq X \leq c) = \dfrac{c-a}{b-a}$ (نسبة الطول).

إذا $X \sim \mathcal{U}([0,1])$: $P(X \leq 0{,}3) = 0{,}3$، $P(0{,}2 \leq X \leq 0{,}7) = 0{,}5$.
:::

## 4. القانون الطبيعي $\mathcal{N}(\mu, \sigma^2)$

::: definition
**تعريف 4.1**

$$f(x) = \frac{1}{\sigma\sqrt{2\pi}}\,e^{-\frac{(x-\mu)^2}{2\sigma^2}}$$

- منحنى جرس متماثل حول $\mu$.
- $E(X) = \mu$، $V(X) = \sigma^2$.
- نقطتا الانعطاف عند $x = \mu \pm \sigma$.
:::

::: property
**⭐ التطبيع**

$$X \sim \mathcal{N}(\mu, \sigma^2) \Rightarrow Z = \frac{X-\mu}{\sigma} \sim \mathcal{N}(0,1)$$

$$P(a \leq X \leq b) = P\!\left(\frac{a-\mu}{\sigma} \leq Z \leq \frac{b-\mu}{\sigma}\right)$$
:::

::: property
**⭐ الاحتمالات المرجعية**

| الفاصل | الاحتمال |
|---|---|
| $\mu \pm \sigma$ | $\approx 68{,}3\%$ |
| $\mu \pm 2\sigma$ | $\approx 95{,}4\%$ |
| $\mu \pm 3\sigma$ | $\approx 99{,}7\%$ |
:::

::: example
**📝 المثال 1:** درجات امتحان $X \sim \mathcal{N}(12, 4)$ ($\mu=12$، $\sigma=2$).

$P(8 \leq X \leq 16) = P(-2 \leq Z \leq 2) \approx 95{,}4\%$.

$P(X \geq 14) = P(Z \geq 1) \approx 15{,}9\%$.

$P(X \leq 10) = P(Z \leq -1) \approx 15{,}9\%$ (بالتماثل).
:::

::: example
**📝 المثال 2 (الشريحة):** في مصنع، وزن علبة $X \sim \mathcal{N}(500, 25)$ غ. ما نسبة العلب بين $490$ و $510$ غ؟

$Z_1 = (490-500)/5 = -2$، $Z_2 = (510-500)/5 = 2$.

$P(490 \leq X \leq 510) = P(-2 \leq Z \leq 2) \approx 95{,}4\%$.
:::

## 🎯 خلاصة

- $P(a \leq X \leq b) = \int_a^b f(x)\,dx$.
- $\mathcal{U}$: كثافة ثابتة؛ $\mathcal{N}$: جرس غاوس.
- التطبيع: $Z = (X-\mu)/\sigma$.
- القاعدة: $68\%-95\%-99{,}7\%$.
