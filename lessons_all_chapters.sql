# درس: حساب الدوال الأصلية

نتعلم في هذا الدرس تقنيات متعددة لإيجاد الدوال الأصلية: قاعدة الدالة المركّبة، والخطية.

## 1. القاعدة العامة للدوال المركّبة

::: property
**⭐ مبرهنة 1.1 — قاعدة الدالة المركّبة**

إذا كانت $u$ قابلة للاشتقاق وبقيم مناسبة:

| الدالة $f(x)$ | دالة أصلية $F(x)$ | الشرط |
|---|---|---|
| $u'(x) \cdot [u(x)]^n$ | $\dfrac{[u(x)]^{n+1}}{n+1}$ | $n \neq -1$ |
| $\dfrac{u'(x)}{u(x)}$ | $\ln\|u(x)\|$ | $u(x) \neq 0$ |
| $u'(x) \cdot e^{u(x)}$ | $e^{u(x)}$ | |
| $u'(x) \cdot \cos(u(x))$ | $\sin(u(x))$ | |
| $u'(x) \cdot \sin(u(x))$ | $-\cos(u(x))$ | |
| $\dfrac{u'(x)}{\sqrt{u(x)}}$ | $2\sqrt{u(x)}$ | $u(x) > 0$ |
:::

::: remark
**🔑 المبدأ الموجّه:**

عند رؤية حاصل ضرب $u'(x) \cdot g(u(x))$، ابحث عن الأصلية لـ $g$ وركّبها مع $u$.

**تقنية التحقق:** اشتق النتيجة واستعمل قاعدة التركيب.
:::

## 2. أمثلة بالقاعدة $u' \cdot u^n$

::: example
**📝 المثال 1:** أوجد أصلية لـ $f(x) = (2x+3)^4$.

**✅ الحل:** $u = 2x+3$، $u' = 2$.

$$f(x) = \frac{1}{2} \underbrace{2}_{u'} \cdot \underbrace{(2x+3)^4}_{u^4}$$

$$F(x) = \frac{1}{2} \cdot \frac{(2x+3)^5}{5} = \frac{(2x+3)^5}{10}$$

**تحقق:** $F'(x) = \frac{5(2x+3)^4 \cdot 2}{10} = (2x+3)^4$ ✓
:::

::: example
**📝 المثال 2:** أوجد أصلية لـ $f(x) = 3x^2(x^3-1)^7$.

**✅ الحل:** $u = x^3-1$، $u' = 3x^2$.

$$f(x) = u'(x) \cdot u(x)^7 \Rightarrow F(x) = \frac{(x^3-1)^8}{8}$$
:::

::: example
**📝 المثال 3:** أوجد أصلية لـ $f(x) = \dfrac{x}{\sqrt{x^2+1}}$.

**✅ الحل:** $u = x^2+1$، $u' = 2x$.

$$f(x) = \frac{x}{\sqrt{x^2+1}} = \frac{1}{2} \cdot \frac{u'(x)}{\sqrt{u(x)}}$$

$$F(x) = \frac{1}{2} \cdot 2\sqrt{u(x)} = \sqrt{x^2+1}$$

**تحقق:** $(\sqrt{x^2+1})' = \dfrac{2x}{2\sqrt{x^2+1}} = \dfrac{x}{\sqrt{x^2+1}}$ ✓
:::

## 3. أمثلة بالقاعدة $u'/u$

::: example
**📝 المثال 4:** أوجد أصلية لـ $f(x) = \dfrac{2x}{x^2+3}$.

**✅ الحل:** $u = x^2+3 > 0$، $u' = 2x$.

$$F(x) = \ln(x^2+3)$$
:::

::: example
**📝 المثال 5:** أوجد أصلية لـ $f(x) = \dfrac{x^2}{x^3-2}$ على $]2^{1/3}\,;\,+\infty[$.

**✅ الحل:** $u = x^3-2$، $u' = 3x^2$.

$$f(x) = \frac{x^2}{x^3-2} = \frac{1}{3} \cdot \frac{3x^2}{x^3-2} = \frac{1}{3} \cdot \frac{u'}{u}$$

$$F(x) = \frac{\ln(x^3-2)}{3}$$
:::

::: example
**📝 المثال 6:** أوجد أصلية لـ $\tan x$.

$$\tan x = \frac{\sin x}{\cos x} = -\frac{-\sin x}{\cos x} = -\frac{(\cos x)'}{\cos x}$$

$$F(x) = -\ln|\cos x|$$
:::

## 4. أمثلة بالقاعدة $u' \cdot e^u$

::: example
**📝 المثال 7:** أوجد أصلية لـ $f(x) = (2x-1)e^{x^2-x}$.

**✅ الحل:** $u = x^2-x$، $u' = 2x-1$.

$$F(x) = e^{x^2-x}$$
:::

::: example
**📝 المثال 8:** أوجد أصلية لـ $f(x) = x^2 e^{x^3}$.

**✅ الحل:** $u = x^3$، $u' = 3x^2$.

$$f(x) = \frac{1}{3} \cdot 3x^2 \cdot e^{x^3} = \frac{1}{3} u'(x) e^{u(x)}$$

$$F(x) = \frac{e^{x^3}}{3}$$
:::

## 5. الخطية

::: property
**⭐ مبرهنة الخطية**

إذا كانت $F$ أصلية لـ $f$ و $G$ أصلية لـ $g$ على $I$، فلكل $\lambda, \mu \in \mathbb{R}$:
$$\lambda F + \mu G \text{ هي أصلية لـ } \lambda f + \mu g$$

**مثال:** $\int (3f - 2g) = 3F - 2G$
:::

::: example
**📝 المثال 9:** أوجد أصلية لـ $f(x) = 3x^2 - \dfrac{2}{x} + 5e^x - 4\sin x$ على $]0\,;\,+\infty[$.

$$F(x) = 3 \cdot \frac{x^3}{3} - 2\ln x + 5e^x - 4(-\cos x) = x^3 - 2\ln x + 5e^x + 4\cos x$$
:::

## 6. أمثلة مركّبة على الدوال المثلثية

::: example
**📝 المثال 10:** أوجد أصلية لـ $f(x) = \cos(3x+1)$.

**✅ الحل:** $u = 3x+1$، $u' = 3$.

$$f(x) = \frac{1}{3} \cdot 3\cos(3x+1) = \frac{1}{3} u'(x)\cos(u(x))$$

$$F(x) = \frac{\sin(3x+1)}{3}$$
:::

::: example
**📝 المثال 11 (تكميلي):** أوجد أصلية لـ $f(x) = \sin^3 x \cos x$.

**✅ الحل:** $u = \sin x$، $u' = \cos x$.

$$f(x) = u(x)^3 \cdot u'(x)$$

$$F(x) = \frac{\sin^4 x}{4}$$
:::

## 7. حالات خاصة تستوجب الانتباه

::: remark
**⚠️ أخطاء شائعة:**

1. $(3x+1)^4 \neq \dfrac{(3x+1)^5}{5}$ ← يجب قسمة على $3$ (مشتقة الداخل).

2. $e^{x^2} \neq e^{x^2} + c$ مباشرة ← $\dfrac{d}{dx}e^{x^2} = 2xe^{x^2} \neq e^{x^2}$.

3. $\dfrac{1}{x^2+1}$ ليست من نوع $u'/u$. أصليتها $\arctan x$.
:::

## 🎯 خلاصة

$$\boxed{(u^{n+1}/(n+1))' = u' \cdot u^n} \qquad \boxed{(\ln|u|)' = u'/u} \qquad \boxed{(e^u)' = u' \cdot e^u}$$

- الخطية: أصلية $\lambda f + \mu g$ هي $\lambda F + \mu G$.
- المبدأ: حدّد $u$ بحيث $u'$ ظاهر في التعبير.
- التحقق دائماً: اشتق نتيجتك.
