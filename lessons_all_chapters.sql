# درس: التزايد المقارن

يدرس هذا الدرس مقارنة السرعة التي تؤول بها دوال مختلفة إلى اللانهاية عند $\pm\infty$. هذا المفهوم أساسي لحل الحدود المبهمة وفهم سلوك الدوال في اللانهاية.

## 1. النتائج الأساسية

::: property
**⭐ مبرهنة 1.1 — التزايد المقارن ($\exp$ مقابل كثيرات الحدود)**

لكل $n \in \mathbb{N}$:

$$\lim_{x \to +\infty} \frac{e^x}{x^n} = +\infty \qquad \Leftrightarrow \qquad \lim_{x \to +\infty} \frac{x^n}{e^x} = 0$$

$$\lim_{x \to -\infty} x^n e^x = 0 \quad \text{(كل قدرة لـ $x$ مضروبة في $e^x$ تؤول إلى $0$)}$$

**المعنى الحدسي:** $e^x$ تنمو بسرعة تفوق **أي** كثيرة حدود مهما كانت درجتها.

بعبارة أخرى: عند $+\infty$، $e^x \gg x^n$ لكل $n$.
:::

::: property
**⭐ مبرهنة 1.2 — التزايد المقارن ($\ln$ مقابل كثيرات الحدود)**

لكل $\alpha > 0$:

$$\lim_{x \to +\infty} \frac{\ln x}{x^\alpha} = 0 \qquad \Leftrightarrow \qquad \lim_{x \to +\infty} \frac{x^\alpha}{\ln x} = +\infty$$

$$\lim_{x \to 0^+} x^\alpha \ln x = 0$$

**المعنى:** $\ln x$ تنمو أبطأ من **أي** قوة موجبة لـ $x$.

عند $0^+$: $\ln x \to -\infty$ لكن أبطأ بكثير من أي $x^{-\alpha}$.
:::

## 2. جدول النهايات المرجعية الشامل

::: property
**⭐ نهايات مرجعية يجب حفظها**

| النهاية | القيمة | نوع الإبهام المحلول |
|---|---|---|
| $\lim\limits_{x \to +\infty} \dfrac{e^x}{x^n}$ | $+\infty$ | $\frac{\infty}{\infty}$ |
| $\lim\limits_{x \to -\infty} x^n e^x$ | $0$ | $\infty \cdot 0$ |
| $\lim\limits_{x \to 0} \dfrac{e^x - 1}{x}$ | $1$ | $\frac{0}{0}$ |
| $\lim\limits_{x \to 0} \dfrac{e^{ax} - 1}{x}$ | $a$ | $\frac{0}{0}$ |
| $\lim\limits_{x \to +\infty} \dfrac{\ln x}{x^\alpha}$ ($\alpha > 0$) | $0$ | $\frac{\infty}{\infty}$ |
| $\lim\limits_{x \to 0^+} x^\alpha \ln x$ ($\alpha > 0$) | $0$ | $0 \cdot (-\infty)$ |
| $\lim\limits_{x \to 0} \dfrac{\ln(1+x)}{x}$ | $1$ | $\frac{0}{0}$ |
| $\lim\limits_{x \to 1} \dfrac{\ln x}{x-1}$ | $1$ | $\frac{0}{0}$ |
:::

## 3. تراتبية الدوال

::: property
**⭐ التراتبية العامة عند $+\infty$**

$$\ln x \ll x^\alpha \ll x^\beta \ll e^x \quad (\text{لكل } 0 < \alpha < \beta)$$

بمعنى لكل دالتين $f \ll g$: $\dfrac{f(x)}{g(x)} \xrightarrow[x\to+\infty]{} 0$.

**قراءة:** اللوغاريتم يخسر أمام أي قوة، وكل قوة تخسر أمام الأسية.
:::

## 4. أمثلة تطبيقية مفصّلة

::: example
**📝 المثال 1:** احسب $\lim\limits_{x \to +\infty} (x^3 - e^x)$.

**✅ الحل:** نعزل الدالة الأقوى ($e^x$):
$$x^3 - e^x = e^x\left(\frac{x^3}{e^x} - 1\right)$$

بما أن $\dfrac{x^3}{e^x} \to 0$ (التزايد المقارن) و $e^x \to +\infty$:
$$e^x \times (0 - 1) = -e^x \to -\infty$$

$$\boxed{\lim_{x \to +\infty}(x^3 - e^x) = -\infty}$$

**تفسير:** الأسية تسحق كثيرة الحدود عند $+\infty$.
:::

::: example
**📝 المثال 2:** احسب $\lim\limits_{x \to +\infty} \dfrac{e^{2x} - 3e^x}{e^{2x} + 1}$.

**✅ الحل:** نقسم بسطاً ومقاماً على $e^{2x}$:
$$\frac{e^{2x} - 3e^x}{e^{2x} + 1} = \frac{1 - 3e^{-x}}{1 + e^{-2x}} \xrightarrow[x\to+\infty]{} \frac{1 - 0}{1 + 0} = 1$$

$$\boxed{\lim_{x \to +\infty} \frac{e^{2x} - 3e^x}{e^{2x} + 1} = 1}$$
:::

::: example
**📝 المثال 3:** احسب $\lim\limits_{x \to +\infty} x^5 e^{-2x}$.

$$x^5 e^{-2x} = \frac{x^5}{e^{2x}}$$

نضع $t = 2x$ (عند $x\to+\infty$، $t\to+\infty$):
$$\frac{x^5}{e^{2x}} = \frac{(t/2)^5}{e^t} = \frac{t^5}{32 e^t} \to 0$$

$$\boxed{\lim_{x \to +\infty} x^5 e^{-2x} = 0}$$
:::

::: example
**📝 المثال 4:** احسب $\lim\limits_{x \to 0^+} x^{1/3} \ln x$.

هذا من نوع $x^\alpha \ln x$ مع $\alpha = 1/3 > 0$:

$$\boxed{\lim_{x \to 0^+} x^{1/3} \ln x = 0}$$

**تذكير للبرهان:** نضع $t = \ln x$ ($x = e^t$، وعند $x\to 0^+$: $t\to-\infty$):
$$x^{1/3}\ln x = e^{t/3} \cdot t = \frac{t}{e^{-t/3}} \to 0$$

لأن $|t| \ll e^{|t|/3}$ عند $t\to-\infty$.
:::

::: example
**📝 المثال 5:** احسب $\lim\limits_{x \to 0} \dfrac{e^{3x} - 1}{x}$.

$$\frac{e^{3x} - 1}{x} = 3 \cdot \frac{e^{3x} - 1}{3x} \xrightarrow[3x\to0]{} 3 \times 1 = 3$$

$$\boxed{\lim_{x \to 0} \frac{e^{3x}-1}{x} = 3}$$
:::

::: example
**📝 المثال 6:** احسب $\lim\limits_{x \to 1} \dfrac{e^x - e}{x - 1}$.

نضع $t = x - 1$ (عند $x\to 1$: $t\to 0$):
$$\frac{e^x - e}{x-1} = \frac{e \cdot e^t - e}{t} = e \cdot \frac{e^t - 1}{t} \to e \times 1 = e$$

$$\boxed{\lim_{x \to 1} \frac{e^x - e}{x-1} = e}$$

**ملاحظة:** هذا هو تعريف المشتقة! $f'(1) = e$ للدالة $f(x) = e^x$.
:::

## 5. تطبيق في الدراسة الكاملة

::: example
**📝 المثال 7:** ادرس $f(x) = (x^2 - x)e^{-x} + 1$ على $\mathbb{R}$.

**✅ النهايات:**

$\lim\limits_{x \to +\infty} f(x)$: $x^2 e^{-x} \to 0$ و $xe^{-x} \to 0$، إذن $f(x) \to 1$.

مقارب أفقي $y = 1$ عند $+\infty$.

$\lim\limits_{x \to -\infty} f(x)$: $e^{-x} \to +\infty$ و $x^2 - x \to +\infty$، إذن $f(x) \to +\infty$.

**المشتقة:**
$$f'(x) = (2x-1)e^{-x} + (x^2-x)(-e^{-x}) = e^{-x}[(2x-1) - (x^2-x)]$$
$$= e^{-x}[-x^2 + 3x - 1]$$

$$f'(x) = 0 \iff -x^2+3x-1 = 0 \iff x = \frac{3 \pm \sqrt{5}}{2}$$

$x_1 = \dfrac{3-\sqrt{5}}{2} \approx 0{,}38$ و $x_2 = \dfrac{3+\sqrt{5}}{2} \approx 2{,}62$.

| $x$ | $-\infty$ | $x_1$ | $x_2$ | $+\infty$ |
|---|---|---|---|---|
| $f'(x)$ | $-$ | $0$ | $+$ $0$ | $-$ |
| $f(x)$ | $+\infty\searrow$ | min | $\nearrow$ | max$\searrow$ | $1$ |
:::

## 🎯 خلاصة

$$\boxed{\frac{e^x}{x^n} \to +\infty, \quad x^n e^x \to 0 \text{ عند } +\infty/-\infty}$$

$$\boxed{\frac{\ln x}{x^\alpha} \to 0, \quad x^\alpha \ln x \to 0 \text{ عند } +\infty/0^+}$$

**التراتبية:** $\ln x \ll x^\alpha \ll e^x$ عند $+\infty$.

**التقنية:** عند الحدود المبهمة، عزل الدالة الأقوى كعامل مشترك.
