# درس: المعادلات التفاضلية

المعادلة التفاضلية هي معادلة تربط دالة مجهولة بمشتقاتها. تُستخدم لوصف النمو، التحلل، الحركة، والظواهر الفيزيائية المختلفة.

## 1. المعادلة التفاضلية الخطية من الرتبة الأولى $y' = ay$

::: definition
**تعريف 1.1**

**معادلة تفاضلية** هي علاقة من الشكل $F(x, y, y', y'', \ldots) = 0$.

أبسط أنواعها: $y' = ay$ حيث $a$ ثابت حقيقي.

**المجهول:** دالة $y(x)$ (لا عدداً).
:::

::: property
**⭐ مبرهنة 1.1 — الحلّ العام لـ $y' = ay$**

لكل $a \in \mathbb{R}$، حلول المعادلة $y' = ay$ هي جميع الدوال:

$$\boxed{y(x) = C e^{ax}, \quad C \in \mathbb{R}}$$

**البرهان:**
- إذا $y = Ce^{ax}$: $y' = Ca e^{ax} = a(Ce^{ax}) = ay$ ✓
- إذا $y' = ay$: نكتب $\dfrac{y'}{y} = a$، فنتكامل: $\ln|y| = ax + k$، إذن $y = Ce^{ax}$.
:::

::: example
**📝 المثال 1:** الحلّ العام لـ $y' = 3y$:

$$y(x) = Ce^{3x}, \quad C \in \mathbb{R}$$

**مع الشرط الأولي $y(0) = 2$:**
$$y(0) = Ce^0 = C = 2 \Rightarrow \boxed{y(x) = 2e^{3x}}$$
:::

::: example
**📝 المثال 2:** الحلّ العام لـ $y' = -\dfrac{1}{2}y$:

$$y(x) = Ce^{-x/2}$$

**مع $y(2) = e$:**
$$Ce^{-1} = e \Rightarrow C = e^2 \Rightarrow y(x) = e^2 \cdot e^{-x/2} = e^{2-x/2}$$
:::

## 2. المعادلة $y' = ay + b$

::: property
**⭐ مبرهنة 2.1 — الحلّ العام**

لكل $a \neq 0$ و $b \in \mathbb{R}$، حلّ $y' = ay + b$:

$$\boxed{y(x) = Ce^{ax} - \frac{b}{a}, \quad C \in \mathbb{R}}$$

**الطريقة المنهجية:**
1. جد **الحلّ الخاص الثابت** $y^*$: ضع $y' = 0$: $0 = ay^* + b \Rightarrow y^* = -b/a$.
2. الحلّ العام = $Ce^{ax} + y^*$.
:::

::: example
**📝 المثال 3:** حلّ $y' = 2y - 4$.

**الحلّ الثابت:** $0 = 2y^* - 4 \Rightarrow y^* = 2$.

**الحلّ العام:** $y(x) = Ce^{2x} + 2$.

**مع $y(0) = 5$:**
$$5 = C + 2 \Rightarrow C = 3 \Rightarrow y(x) = 3e^{2x} + 2$$

**تحقق:** $y'(x) = 6e^{2x}$، $2y-4 = 6e^{2x}+4-4 = 6e^{2x}$ ✓
:::

::: example
**📝 المثال 4:** حلّ $y' + 3y = 6$، $y(0) = 1$.

نعيد الكتابة: $y' = -3y + 6$.

**الحلّ الثابت:** $0 = -3y^* + 6 \Rightarrow y^* = 2$.

**الحلّ العام:** $y(x) = Ce^{-3x} + 2$.

$y(0) = C + 2 = 1 \Rightarrow C = -1$.

$$\boxed{y(x) = -e^{-3x} + 2}$$
:::

## 3. أشكال مختلفة وتبسيطها

::: example
**📝 المثال 5:** حلّ $2y' - 6y = 0$.

نقسم على $2$: $y' = 3y$.

$$y(x) = Ce^{3x}$$
:::

::: example
**📝 المثال 6:** حلّ $(y+1)' = 2(y+1)$.

نضع $z = y+1$: $z' = 2z$.

$$z(x) = Ce^{2x} \Rightarrow y(x) = Ce^{2x} - 1$$
:::

## 4. تطبيقات — النمو والتحلل

::: example
**📝 المثال 7 — النمو البكتيري:**

عدد بكتيريا $N(t)$ يتبع: $N'(t) = 0{,}5\,N(t)$، $N(0) = 1000$.

$$N(t) = 1000\,e^{0{,}5t}$$

**وقت التضاعف:** $N(t_0) = 2000 \Rightarrow e^{0{,}5t_0} = 2 \Rightarrow t_0 = \dfrac{\ln 2}{0{,}5} = 2\ln 2 \approx 1{,}39$ ساعة.

**عدد البكتيريا بعد $4$ ساعات:** $N(4) = 1000\,e^2 \approx 7\,389$.
:::

::: example
**📝 المثال 8 — التحلل الإشعاعي:**

$m'(t) = -\lambda m(t)$، $m(0) = m_0$.

$$m(t) = m_0 e^{-\lambda t}$$

**نصف العمر** $T_{1/2}$: $m(T_{1/2}) = m_0/2$:
$$e^{-\lambda T_{1/2}} = \frac{1}{2} \Rightarrow T_{1/2} = \frac{\ln 2}{\lambda}$$

**للكربون 14:** $\lambda \approx 1{,}21 \times 10^{-4}$/سنة، $T_{1/2} \approx 5730$ سنة.
:::

::: example
**📝 المثال 9 — قانون نيوتن للتبريد:**

درجة حرارة جسم $T(t)$ في وسط حرارته $T_{\text{env}} = 20°C$:

$$T'(t) = -k(T(t) - 20), \quad T(0) = 80°C$$

نضع $u = T - 20$: $u' = -ku$، إذن $u = Ce^{-kt}$.

$u(0) = 80 - 20 = 60 \Rightarrow C = 60$.

$$T(t) = 20 + 60e^{-kt}$$

إذا $T(10) = 50$: $50 = 20 + 60e^{-10k} \Rightarrow e^{-10k} = 1/2 \Rightarrow k = \ln 2/10$.
:::

## 5. المعادلة مع شرطين

::: example
**📝 المثال 10:** دالة $f$ تحقق $f' - 2f = 0$ و $f(1) = 3e^2$.

الحلّ العام: $f(x) = Ce^{2x}$.

$f(1) = Ce^2 = 3e^2 \Rightarrow C = 3$.

$$\boxed{f(x) = 3e^{2x}}$$
:::

## 🎯 خلاصة

$$\boxed{y' = ay \Rightarrow y = Ce^{ax}}$$
$$\boxed{y' = ay + b \Rightarrow y = Ce^{ax} - \frac{b}{a}}$$

- الشرط الأولي يُحدّد ثابت $C$.
- الحلّ الثابت: الحلّ بشرط $y' = 0$.
- تطبيقات: نمو/تحلل/تبريد.
