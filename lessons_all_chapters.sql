# درس: العد — القوائم والترتيبات والتبديلات

علم العد يُمكّننا من حساب عدد الطرق الممكنة لترتيب أو اختيار عناصر. هذا الأساس ضروري لحساب الاحتمالات في الفضاءات المتماثلة.

## 1. مبدأ الضرب (القياس)

::: property
**⭐ مبدأ الضرب الأساسي**

إذا تألف اختيار من $k$ مراحل متتالية مستقلة بحيث:
- المرحلة $i$ لها $n_i$ اختياراً.

فإن المجموع الكلي = $n_1 \times n_2 \times \cdots \times n_k$.
:::

::: example
**📝 المثال 1:** لوحة سيارة = $2$ حروف من $\{A,\ldots,Z\}$ + $4$ أرقام من $\{0,\ldots,9\}$ (مع تكرار).

$$26^2 \times 10^4 = 676 \times 10\,000 = 6\,760\,000 \text{ لوحة}$$
:::

::: example
**📝 المثال 2:** فصل من $30$ طالباً يختار رئيساً ونائباً وأمين سرّ (ثلاثة أشخاص مختلفون).

$$30 \times 29 \times 28 = 24\,360$$
:::

## 2. القوائم (مع الترتيب، مع التكرار)

::: definition
**تعريف 2.1 — القائمة المرتبة**

قائمة من $p$ عناصر من مجموعة $E$ ذات $n$ عنصراً (التكرار مسموح، الترتيب مهمّ):

$$\text{عدد القوائم} = n^p$$
:::

::: example
**📝 المثال 3:** رمز PIN مكوّن من $4$ أرقام (الرقم يمكن تكراره): $10^4 = 10\,000$.

**📝 المثال 4:** كلمة سرّ من $8$ حروف + أرقام (62 رمزاً): $62^8 \approx 2{,}18 \times 10^{14}$.
:::

## 3. الترتيبات (مع الترتيب، دون تكرار)

::: definition
**تعريف 3.1 — الترتيب**

ترتيب من $p$ عناصر مختلفة من $n$ ($p \leq n$):

$$\boxed{A_n^p = \frac{n!}{(n-p)!} = n(n-1)(n-2)\cdots(n-p+1)}$$

مع $n! = n \times (n-1) \times \cdots \times 1$ و $0! = 1$.
:::

::: property
**⭐ تفسير المفهوم**

نختار $p$ عنصراً من $n$ دون تكرار ونضعها في ترتيب معين:
- الاختيار الأول: $n$ طريقة.
- الاختيار الثاني: $n-1$ طريقة (عنصر محذوف).
- ...
- الاختيار الـ$p$: $n-p+1$ طريقة.
:::

::: example
**📝 المثال 5:** كم طريقة لترتيب $3$ كتب على رفّ من $8$ كتب مختلفة؟

$$A_8^3 = 8 \times 7 \times 6 = 336$$
:::

::: example
**📝 المثال 6:** كم رقماً رباعياً بلا تكرار من $\{1,\ldots,9\}$؟

$$A_9^4 = 9 \times 8 \times 7 \times 6 = 3\,024$$

**مع شرط إضافي (الرقم فردي):** الرقم الأول (الآحاد) من $\{1,3,5,7,9\}$ (5 اختيارات)، الباقي: $A_8^3 = 336$.

$$5 \times 336 = 1\,680$$
:::

## 4. التبديلات

::: definition
**تعريف 4.1 — التبديل**

تبديل لـ $n$ عنصراً = ترتيب لجميع العناصر الـ$n$:

$$\boxed{P_n = A_n^n = n!}$$
:::

::: example
**📝 المثال 7:** الجدول الحسابي:

| $n$ | $n!$ |
|---|---|
| $0$ | $1$ |
| $3$ | $6$ |
| $5$ | $120$ |
| $7$ | $5040$ |
| $10$ | $3\,628\,800$ |
:::

::: example
**📝 المثال 8 (مع قيود):** كم طريقة لترتيب $4$ أولاد و $3$ بنات في صف بحيث تكون البنات متجاورات؟

**✅ الحل:** نعامل البنات وحدة واحدة ($1$ + $4$ = $5$ عناصر):

- ترتيب الـ$5$ عناصر: $5! = 120$.
- ترتيب البنات بينهن: $3! = 6$.

$$\text{المجموع} = 5! \times 3! = 120 \times 6 = 720$$
:::

## 5. الجدول المقارن

::: property
**⭐ ملخص مقارن**

| النوع | الترتيب؟ | التكرار؟ | الصيغة | مثال |
|---|---|---|---|---|
| قائمة | نعم | نعم | $n^p$ | رمز PIN |
| ترتيب | نعم | لا | $A_n^p$ | كتب على رفّ |
| تبديل | نعم | لا | $n!$ | سباق |
| توفيق | لا | لا | $C_n^p$ | لجنة (الدرس القادم) |
:::

## 🎯 خلاصة

$$\boxed{A_n^p = \frac{n!}{(n-p)!}} \qquad n! = n \times (n-1) \times \cdots \times 1$$

- مع التكرار + الترتيب: $n^p$.
- بلا تكرار + مع الترتيب: $A_n^p$.
- تبديل (ترتيب الكل): $n!$.
