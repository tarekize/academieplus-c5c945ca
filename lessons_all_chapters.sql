:::

::: example
**📝 المثال 3 (الحدّ العام):** أوجد الحدّ المحتوي على $x^3$ في $(x + 1/x)^7$.

الحدّ العام: $C_7^k x^{7-k} (1/x)^k = C_7^k x^{7-2k}$.

$7 - 2k = 3 \Rightarrow k = 2$.

الحدّ = $C_7^2 x^3 = 21x^3$.
