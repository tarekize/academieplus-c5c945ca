# درس: التمديد إلى دالة إشارتها كيفية

عندما لا تحافظ الدالة على إشارتها، يختلف مفهوم **المساحة الحقيقية** عن **التكامل الجبري**.

## 1. الفرق بين المساحة الجبرية والحقيقية

::: property
**⭐ المفهومان الأساسيان**

**المساحة الجبرية:**
$$\mathcal{A}_{\text{جبرية}} = \int_a^b f(x)\,dx$$
قد تكون موجبة أو سالبة (تطرح المساحات تحت المحور).

**المساحة الحقيقية:**
$$\mathcal{A}_{\text{حقيقية}} = \int_a^b |f(x)|\,dx \geq 0$$
دائماً موجبة.

**العلاقة:** $\mathcal{A}_{\text{حقيقية}} \geq |\mathcal{A}_{\text{جبرية}}|$.
:::

::: example
**📝 توضيح الفرق:**

$f(x) = x$ على $[-1\,;\,1]$.

$\int_{-1}^1 x\,dx = [x^2/2]_{-1}^1 = 0$ (جبرية = 0 لأن $f$ فردية).

لكن المساحة الحقيقية $= 2 \int_0^1 x\,dx = 2 \times \frac{1}{2} = 1 \neq 0$.
:::

## 2. المنهجية العامة

::: property
**⭐ خطوات حساب المساحة الحقيقية**

1. **حدّد أصفار $f$** على $[a\,;\,b]$: $x_1 < x_2 < \ldots < x_k$.
2. **قسّم** $[a\,;\,b]$ إلى أجزاء $[a, x_1], [x_1, x_2], \ldots, [x_k, b]$.
3. **على كل جزء** احسب التكامل وخذ قيمته المطلقة.
4. **اجمع** المساحات الجزئية.

$$\mathcal{A} = \sum_i \left|\int_{x_i}^{x_{i+1}} f(x)\,dx\right|$$
:::

## 3. أمثلة تطبيقية مفصّلة

::: example
**📝 المثال 1:** المساحة بين $f(x) = x^2 - 1$ ومحور الفواصل على $[-2\,;\,2]$.

**✅ الحل:**

**أصفار:** $f(x) = 0 \iff x = \pm 1$.

**الإشارة:**
- $[-2\,;\,-1]$: $f \geq 0$ (مثلاً $f(-2) = 3 > 0$).
- $[-1\,;\,1]$: $f \leq 0$ (مثلاً $f(0) = -1 < 0$).
- $[1\,;\,2]$: $f \geq 0$.

**الحساب:**

$\int_{-2}^{-1}(x^2-1)\,dx = \left[\frac{x^3}{3}-x\right]_{-2}^{-1} = \left(-\frac{1}{3}+1\right)-\left(-\frac{8}{3}+2\right) = \frac{2}{3}+\frac{2}{3} = \frac{4}{3}$

$\int_{-1}^{1}(x^2-1)\,dx = \left[\frac{x^3}{3}-x\right]_{-1}^{1} = -\frac{4}{3}$ (سالب كما توقعنا)

$\int_{1}^{2}(x^2-1)\,dx = \frac{4}{3}$ (بالتماثل مع الجزء الأول)

$$\mathcal{A} = \frac{4}{3} + \frac{4}{3} + \frac{4}{3} = 4 \text{ وحدة مربعة}$$
:::

::: example
**📝 المثال 2:** المساحة بين $f(x) = x^3 - x$ ومحور الفواصل على $[-1\,;\,2]$.

**أصفار:** $x(x-1)(x+1) = 0 \Rightarrow x \in \{-1, 0, 1\}$.

**الإشارة على $[-1\,;\,2]$:**
- $[-1\,;\,0]$: $f \geq 0$ (مثلاً $f(-0{,}5) = -0{,}125+0{,}5 > 0$).
- $[0\,;\,1]$: $f \leq 0$.
- $[1\,;\,2]$: $f \geq 0$.

**الحساب (أصلية: $F(x) = x^4/4 - x^2/2$):**

$\int_{-1}^0 f = F(0)-F(-1) = 0 - (1/4-1/2) = 1/4$

$\int_0^1 f = F(1)-F(0) = (1/4-1/2) - 0 = -1/4$

$\int_1^2 f = F(2)-F(1) = (4-2) - (1/4-1/2) = 2+1/4 = 9/4$

$$\mathcal{A} = \frac{1}{4} + \frac{1}{4} + \frac{9}{4} = \frac{11}{4} \text{ وحدة مربعة}$$
:::

## 4. المساحة بين منحنيين

::: property
**⭐ الصيغة العامة**

مساحة الحقل المحاط بين $f$ و $g$ على $[a\,;\,b]$:

$$\mathcal{A} = \int_a^b |f(x) - g(x)|\,dx$$

**المنهجية:**
1. أوجد نقاط التقاطع ($f(x) = g(x)$).
2. حدّد أيهما أكبر على كل جزء.
3. احسب التكامل المناسب.
:::

::: example
**📝 المثال 3:** مساحة الحقل المحاط بـ $f(x) = x^2$ و $g(x) = x$.

**نقاط التقاطع:** $x^2 = x \Rightarrow x = 0$ أو $x = 1$.

على $[0\,;\,1]$: $g(x) = x \geq x^2 = f(x)$.

$$\mathcal{A} = \int_0^1 (x-x^2)\,dx = \left[\frac{x^2}{2}-\frac{x^3}{3}\right]_0^1 = \frac{1}{2}-\frac{1}{3} = \frac{1}{6}$$
:::

::: example
**📝 المثال 4:** مساحة بين $f(x) = e^x$ و $g(x) = x + 1$ على $[-1\,;\,1]$.

$e^x \geq x+1$ لكل $x$ (معروف بأن $e^x \geq 1+x$)، والتساوي عند $x=0$.

$$\mathcal{A} = \int_{-1}^{1} (e^x - x - 1)\,dx = [e^x - x^2/2 - x]_{-1}^1$$
$$= (e - 1/2 - 1) - (e^{-1} - 1/2 + 1) = e - 3/2 - e^{-1} + 1/2 - 1 = e - e^{-1} - 2$$
$$\approx 2{,}718 - 0{,}368 - 2 = 0{,}350 \text{ وحدة مربعة}$$
:::

## 🎯 خلاصة

- $\int_a^b f\,dx$ = مساحة **جبرية** (قد تكون سالبة).
- $\int_a^b |f|\,dx$ = مساحة **حقيقية** (دائماً موجبة).
- قسّم عند أصفار $f$ واجمع القيم المطلقة.
- بين منحنيين: $\mathcal{A} = \int_a^b |f-g|\,dx$.
