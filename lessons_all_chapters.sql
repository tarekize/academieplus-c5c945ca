# درس: توظيف الحساب التكاملي لحساب دوال أصلية

نتعلم تقنيتين متقدمتين: **التكامل بالتجزئة** و**التغيير المتغير**.

## 1. التكامل بالتجزئة

::: property
**⭐ المبرهنة**

إذا كانت $u$ و $v$ قابلتين للاشتقاق على $[a\,;\,b]$:

$$\boxed{\int_a^b u(x)\,v'(x)\,dx = \Big[u(x)v(x)\Big]_a^b - \int_a^b u'(x)\,v(x)\,dx}$$

**البرهان:** من قاعدة اشتقاق الجداء: $(uv)' = u'v + uv'$، نُكامل الطرفين.
:::

::: remark
**🔑 اختيار $u$ و $v'$ — قاعدة LIATE:**

نختار $u$ حسب الأولوية: **L**og → **I**nverse trig → **A**lgebraic → **T**rig → **E**xponential.

- $u = \ln x$ (اشتقاقها يُبسّط): $(\ln x)' = 1/x$.
- $u = x^n$ (اشتقاقها يُخفّض الدرجة).
- $v' = e^x$، $\cos x$، $\sin x$ (سهلة التكامل).
:::

::: example
**📝 المثال 1:** $\int_0^1 x\,e^x\,dx$.

$u = x$، $v' = e^x$ → $u' = 1$، $v = e^x$.

$$\int_0^1 xe^x\,dx = [xe^x]_0^1 - \int_0^1 e^x\,dx = (e-0) - [e^x]_0^1 = e - (e-1) = 1$$
:::

::: example
**📝 المثال 2:** $\int_1^e \ln x\,dx$.

$u = \ln x$، $v' = 1$ → $u' = 1/x$، $v = x$.

$$[x\ln x]_1^e - \int_1^e 1\,dx = (e \cdot 1 - 1 \cdot 0) - [x]_1^e = e - (e-1) = 1$$
:::

::: example
**📝 المثال 3:** $\int_0^1 x^2 e^x\,dx$ (تجزئة مزدوجة).

**أولاً:** $u = x^2$، $v' = e^x$:
$$[x^2 e^x]_0^1 - \int_0^1 2x e^x\,dx = e - 2\int_0^1 xe^x\,dx$$

**ثانياً:** $\int_0^1 xe^x\,dx = 1$ (من المثال 1).

$$\int_0^1 x^2 e^x\,dx = e - 2 \times 1 = e - 2$$
:::

::: example
**📝 المثال 4:** $\int_0^\pi x\cos x\,dx$.

$u = x$، $v' = \cos x$ → $u' = 1$، $v = \sin x$.

$$[x\sin x]_0^\pi - \int_0^\pi \sin x\,dx = (0-0) - [-\cos x]_0^\pi = -(\cos\pi - \cos 0) = -(-1-1) = 2$$
:::

::: example
**📝 المثال 5:** $\int_1^e x\ln x\,dx$.

$u = \ln x$، $v' = x$ → $u' = 1/x$، $v = x^2/2$.

$$\left[\frac{x^2\ln x}{2}\right]_1^e - \int_1^e \frac{x}{2}\,dx = \frac{e^2}{2} - \left[\frac{x^2}{4}\right]_1^e = \frac{e^2}{2} - \frac{e^2-1}{4} = \frac{e^2+1}{4}$$
:::

## 2. التغيير المتغير

::: property
**⭐ المبرهنة**

إذا كانت $\phi$ قابلة للاشتقاق على $[\alpha\,;\,\beta]$ وكانت $f$ مستمرة على $\phi([\alpha\,;\,\beta])$:

$$\boxed{\int_\alpha^\beta f(\phi(t))\,\phi'(t)\,dt = \int_{\phi(\alpha)}^{\phi(\beta)} f(x)\,dx}$$

**انتبه:** عند التغيير، يتغيّر الحدّان أيضاً!
:::

::: example
**📝 المثال 6:** $\int_0^1 \dfrac{x}{\sqrt{x^2+1}}\,dx$.

نضع $t = x^2+1$، $dt = 2x\,dx$.

عند $x=0$: $t=1$؛ عند $x=1$: $t=2$.

$$\int_0^1 \frac{x}{\sqrt{x^2+1}}\,dx = \int_1^2 \frac{1}{\sqrt{t}} \cdot \frac{dt}{2} = \frac{1}{2}\left[2\sqrt{t}\right]_1^2 = \sqrt{2}-1$$
:::

::: example
**📝 المثال 7:** $\int_0^{\sqrt{3}} \dfrac{dx}{1+x^2}$ بتغيير $x = \tan\theta$.

$dx = (1+\tan^2\theta)\,d\theta = \cos^{-2}\theta\,d\theta$.

عند $x=0$: $\theta=0$؛ عند $x=\sqrt{3}$: $\theta=\pi/3$.

$$\int_0^{\pi/3} \frac{\cos^{-2}\theta}{1+\tan^2\theta}\,d\theta = \int_0^{\pi/3} 1\,d\theta = \frac{\pi}{3}$$
:::

::: example
**📝 المثال 8 (تطبيقي):** أوجد أصلية لـ $f(x) = \dfrac{1}{\sqrt{1-x^2}}$ بتغيير $x = \sin\theta$.

$dx = \cos\theta\,d\theta$، $\sqrt{1-x^2} = \cos\theta$.

$$\int \frac{\cos\theta\,d\theta}{\cos\theta} = \int 1\,d\theta = \theta + c = \arcsin x + c$$

إذن: **أصلية $\dfrac{1}{\sqrt{1-x^2}}$ هي $\arcsin x$**.
:::

## 🎯 خلاصة

$$\boxed{\int uv' = [uv] - \int u'v}$$

$$\boxed{\int_\alpha^\beta f(\phi(t))\phi'(t)\,dt = \int_{\phi(\alpha)}^{\phi(\beta)} f(x)\,dx}$$

- التجزئة: اختر $u$ ليُبسّط اشتقاقاً.
- التغيير: غيّر الحدّين معاً.
