## 3. دستور ثنائي الحد (نيوتن)

::: property
**⭐ المبرهنة**

$$\boxed{(a+b)^n = \sum_{k=0}^n \binom{n}{k} a^{n-k} b^k}$$
:::

::: example
**📝 المثال 1:** $(x+1)^5$.

$$= x^5 + 5x^4 + 10x^3 + 10x^2 + 5x + 1$$
