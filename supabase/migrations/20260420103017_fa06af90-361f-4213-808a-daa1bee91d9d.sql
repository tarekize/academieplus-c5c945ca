# درس: اتجاه تغير دالة

نستعمل **إشارة المشتقة** لدراسة **اتجاه تغير الدالة** (تزايد/تناقص) وتحديد **القيم الحدية**.

## 1. العلاقة بين إشارة $f'$ واتجاه التغير

::: property
**⭐ مبرهنة 1.1**

لتكن $f$ دالة قابلة للاشتقاق على مجال $I$:

- إذا كان $f'(x) \geq 0$ على $I$، فإن $f$ **متزايدة** على $I$.
- إذا كان $f'(x) \leq 0$ على $I$، فإن $f$ **متناقصة** على $I$.
- إذا كان $f'(x) = 0$ على $I$، فإن $f$ **ثابتة** على $I$.
:::

::: remark
**ملاحظة:** إذا كان $f'(x) > 0$ تماماً (عدا في عدد منته من النقاط)، فإن $f$ **متزايدة تماماً** على $I$.
:::

## 2. القيم الحدية المحلية

::: definition
**تعريف 2.1 — القيمة الحدية المحلية**

نقول إن $f$ تقبل **قيمة حدية محلية** عند $x_0$ إذا غيّرت $f'$ إشارتها عند $x_0$:

- إذا كانت $f'$ موجبة قبل $x_0$ وسالبة بعدها $\Rightarrow$ $f(x_0)$ **قيمة حدية عظمى محلية**.
- إذا كانت $f'$ سالبة قبل $x_0$ وموجبة بعدها $\Rightarrow$ $f(x_0)$ **قيمة حدية صغرى محلية**.
:::

::: remark
**⚠️ تحذير:** $f'(x_0) = 0$ شرط لازم لكن **غير كافٍ**. مثلاً $f(x)=x^3$ مع $f'(0)=0$، لكن لا توجد قيمة حدية عند $0$.
:::

## 3. مثال تطبيقي شامل

::: example
**📝 المثال:** ادرس اتجاه تغير $f(x) = x^3 - 3x + 2$ على $\mathbb{R}$.

**✅ الحل:**

**1) حساب المشتقة:**
$$ f'(x) = 3x^2 - 3 = 3(x-1)(x+1) $$

**2) دراسة إشارة $f'(x)$:**

| $x$ | $-\infty$ |  | $-1$ |  | $1$ |  | $+\infty$ |
|---|---|---|---|---|---|---|---|
| $f'(x)$ |  | $+$ | $0$ | $-$ | $0$ | $+$ |  |

**3) جدول التغيرات:**

نحسب: $f(-1) = -1 + 3 + 2 = 4$ و $f(1) = 1 - 3 + 2 = 0$.

| $x$ | $-\infty$ |  | $-1$ |  | $1$ |  | $+\infty$ |
|---|---|---|---|---|---|---|---|
| $f(x)$ | $-\infty$ | $\nearrow$ | $4$ | $\searrow$ | $0$ | $\nearrow$ | $+\infty$ |

**4) الاستنتاج:**
- $f$ تقبل **قيمة حدية عظمى محلية** قدرها $4$ عند $x = -1$.
- $f$ تقبل **قيمة حدية صغرى محلية** قدرها $0$ عند $x = 1$.
:::

## 4. التفسير البياني

::: graphic
**📈 التفسير البياني**

- في المجالات التي $f' > 0$: المنحنى **يصعد**.
- في المجالات التي $f' < 0$: المنحنى **ينزل**.
- عند نقاط القيم الحدية: المماس **أفقي** ($f'(x_0) = 0$).
:::

## 🎯 خلاصة الدرس

- **إشارة $f'$** $\Rightarrow$ اتجاه تغير $f$.
- **انعدام $f'$ مع تغير الإشارة** $\Rightarrow$ قيمة حدية محلية.
- جدول التغيرات هو الأداة الأساسية لدراسة الدوال.
