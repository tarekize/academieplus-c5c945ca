# درس: اشتقاق دالة مركبة

نتعلم كيفية اشتقاق دالة من شكل $f(x) = g(u(x))$، أي **مركّب دالتين**.

## 1. تذكير بمركّب دالتين

::: definition
**تعريف 1.1**

إذا كانت $u$ دالة معرفة على $I$ بقيم في $J$، و $g$ دالة معرفة على $J$، فإن الدالة $f = g \circ u$ المعرفة بـ:

$$ f(x) = g(u(x)) $$

تُسمى **الدالة المركّبة** للدالتين $u$ و $g$.
:::

## 2. مبرهنة اشتقاق الدالة المركّبة

::: property
**⭐ مبرهنة 2.1 (قاعدة السلسلة)**

إذا كانت $u$ قابلة للاشتقاق على $I$ و $g$ قابلة للاشتقاق على $J$ بحيث $u(I) \subset J$، فإن الدالة $f = g \circ u$ قابلة للاشتقاق على $I$ ولدينا:

$$ f'(x) = u'(x) \cdot g'(u(x)) $$
:::

## 3. تطبيقات مهمة

::: property
**⭐ صيغ مشتقات مركّبات شائعة**

| الدالة | المشتقة | الشرط |
|---|---|---|
| $(u(x))^n$ | $n\,u'(x)\,(u(x))^{n-1}$ | $n \in \mathbb{N}^*$ |
| $\sqrt{u(x)}$ | $\dfrac{u'(x)}{2\sqrt{u(x)}}$ | $u(x) > 0$ |
| $\dfrac{1}{u(x)}$ | $-\dfrac{u'(x)}{(u(x))^2}$ | $u(x) \neq 0$ |
| $\sin(u(x))$ | $u'(x)\,\cos(u(x))$ | — |
| $\cos(u(x))$ | $-u'(x)\,\sin(u(x))$ | — |
| $\sin(ax+b)$ | $a\cos(ax+b)$ | — |
| $\cos(ax+b)$ | $-a\sin(ax+b)$ | — |
:::

## 4. أمثلة تطبيقية

::: example
**📝 المثال 1:** اشتق $f(x) = (3x^2 + 1)^5$.

**✅ الحل:** نضع $u(x) = 3x^2 + 1$، إذن $u'(x) = 6x$:

$$ f'(x) = 5\,u'(x)\,(u(x))^4 = 5 \cdot 6x \cdot (3x^2+1)^4 = 30x(3x^2+1)^4 $$
:::

::: example
**📝 المثال 2:** اشتق $g(x) = \sqrt{x^2 - 4x + 5}$.

**✅ الحل:** نلاحظ أن $x^2-4x+5 = (x-2)^2+1 > 0$، إذن $g$ معرفة على $\mathbb{R}$.

نضع $u(x) = x^2-4x+5$، فإن $u'(x) = 2x-4$:

$$ g'(x) = \frac{u'(x)}{2\sqrt{u(x)}} = \frac{2x-4}{2\sqrt{x^2-4x+5}} = \frac{x-2}{\sqrt{x^2-4x+5}} $$
:::

::: example
**📝 المثال 3:** اشتق $h(x) = \sin(2x + \pi/3)$.

**✅ الحل:** بتطبيق الصيغة المباشرة مع $a=2$:

$$ h'(x) = 2\cos\!\left(2x + \frac{\pi}{3}\right) $$
:::

::: remark
**⚠️ ملاحظات:**
- اعتمد منهجية: حدِّد $u$ ثم $u'$ ثم طبِّق الصيغة.
- لا تنسَ التحقّق من شروط وجود المشتقة (مثلاً $u > 0$ للجذر).
:::

## 🎯 خلاصة الدرس

- **قاعدة السلسلة:** $(g \circ u)' = u' \cdot (g' \circ u)$.
- احفظ الصيغ الجاهزة لـ $u^n$، $\sqrt{u}$، $\sin(u)$، $\cos(u)$.
- منهجية: $u \to u' \to$ تطبيق الصيغة.
