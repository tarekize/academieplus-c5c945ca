# درس: دراسة دالة مثلثية

ندرس في هذا الدرس **الدوال المثلثية** ($\sin$, $\cos$, $\tan$): دوريّتها، شفعيّتها، مشتقاتها واتجاه تغيّرها.

## 1. تذكير بالدوال المثلثية الأساسية

::: property
**⭐ خواص الدوال $\sin$ و $\cos$**

| الخاصية | $\cos x$ | $\sin x$ |
|---|---|---|
| مجال التعريف | $\mathbb{R}$ | $\mathbb{R}$ |
| الدورية | $2\pi$ | $2\pi$ |
| الشفعية | **شفعية**: $\cos(-x)=\cos x$ | **فردية**: $\sin(-x)=-\sin x$ |
| المجال | $[-1\,;\,1]$ | $[-1\,;\,1]$ |
| المشتقة | $-\sin x$ | $\cos x$ |
:::

::: property
**⭐ نهايات و قيم خاصة**

$$ \sin 0 = 0, \quad \sin\frac{\pi}{2} = 1, \quad \sin\pi = 0 $$

$$ \cos 0 = 1, \quad \cos\frac{\pi}{2} = 0, \quad \cos\pi = -1 $$
:::

## 2. الدوريّة والشفعيّة

::: definition
**تعريف 2.1**

- $f$ **دورية** ذات دور $T$ إذا كان $f(x+T) = f(x)$ لكل $x$.
- $f$ **شفعية** إذا كان $f(-x) = f(x)$ (تماثل بالنسبة لمحور التراتيب).
- $f$ **فردية** إذا كان $f(-x) = -f(x)$ (تماثل بالنسبة للمبدأ).
:::

::: remark
**ملاحظة منهجية:** لدراسة دالة مثلثية:
1. حدّد **الدور** $T$ $\Rightarrow$ ادرس على مجال طوله $T$.
2. حدّد **الشفعية** $\Rightarrow$ قلّص المجال إلى نصف.
3. ادرس $f$ على المجال المختزل.
4. أكمل المنحنى بالتماثل والإزاحة.
:::

## 3. مثال تطبيقي شامل

::: example
**📝 المثال:** ادرس الدالة $f(x) = \cos(2x)$ على $\mathbb{R}$.

**✅ الحل:**

**1) الدور:** نلاحظ أن $\cos(2(x+\pi)) = \cos(2x+2\pi) = \cos(2x)$. إذن $T = \pi$.

نختزل الدراسة على $[0\,;\,\pi]$.

**2) الشفعية:** $f(-x) = \cos(-2x) = \cos(2x) = f(x)$. إذن $f$ **شفعية**.

نختزل أكثر إلى $[0\,;\,\pi/2]$.

**3) المشتقة:** $f'(x) = -2\sin(2x)$.

**4) إشارة $f'$ على $[0\,;\,\pi/2]$:**

$2x \in [0\,;\,\pi]$ إذن $\sin(2x) \geq 0$، ومنه $f'(x) \leq 0$.

**5) جدول التغيرات على $[0\,;\,\pi/2]$:**

| $x$ | $0$ |  | $\pi/2$ |
|---|---|---|---|
| $f'(x)$ | $0$ | $-$ | $0$ |
| $f(x)$ | $1$ | $\searrow$ | $-1$ |

**6) الاستنتاج:** $f$ متناقصة تماماً من $1$ إلى $-1$ على $[0\,;\,\pi/2]$، ثم نُكمل بالتماثل والدوريّة.
:::

## 4. التفسير البياني

::: graphic
**📈 التفسير البياني**

- المنحنى يتكرّر كل $T$ على محور الفواصل.
- إذا كانت $f$ **شفعية**: تماثل بالنسبة لـ $(Oy)$.
- إذا كانت $f$ **فردية**: تماثل بالنسبة للمبدأ $O$.
- النقاط التي $f'(x) = 0$ هي **قمم** أو **قيعان** المنحنى.
:::

## 🎯 خلاصة الدرس

- $(\sin x)' = \cos x$ و $(\cos x)' = -\sin x$.
- $\sin(ax+b))' = a\cos(ax+b)$ و $(\cos(ax+b))' = -a\sin(ax+b)$.
- استغلّ **الدوريّة** و **الشفعيّة** لاختزال مجال الدراسة.
