# درس: المشتقات والعمليات

نتناول في هذا الدرس **الدوال المشتقة المرجعية** و **قواعد الاشتقاق** على العمليات.

## 1. الدوال المشتقة للدوال المرجعية

::: property
**⭐ جدول المشتقات المرجعية**

| الدالة $f(x)$ | المشتقة $f'(x)$ | مجال الاشتقاقية |
|---|---|---|
| $k$ (ثابت) | $0$ | $\mathbb{R}$ |
| $x$ | $1$ | $\mathbb{R}$ |
| $x^n,\ n \in \mathbb{N}^*$ | $n\,x^{n-1}$ | $\mathbb{R}$ |
| $\dfrac{1}{x}$ | $-\dfrac{1}{x^2}$ | $\mathbb{R}^*$ |
| $\dfrac{1}{x^n}$ | $-\dfrac{n}{x^{n+1}}$ | $\mathbb{R}^*$ |
| $\sqrt{x}$ | $\dfrac{1}{2\sqrt{x}}$ | $]0\,;\,+\infty[$ |
| $\sin x$ | $\cos x$ | $\mathbb{R}$ |
| $\cos x$ | $-\sin x$ | $\mathbb{R}$ |
| $\tan x$ | $1 + \tan^2 x = \dfrac{1}{\cos^2 x}$ | $\mathbb{R}\setminus\left\{\frac{\pi}{2}+k\pi\right\}$ |
:::

## 2. قواعد الاشتقاق على العمليات

::: property
**⭐ خواص العمليات على المشتقات**

لتكن $u$ و $v$ دالتين قابلتين للاشتقاق و $k \in \mathbb{R}$:

| العملية | المشتقة |
|---|---|
| $(k\,u)'$ | $k\,u'$ |
| $(u + v)'$ | $u' + v'$ |
| $(u \cdot v)'$ | $u'\,v + u\,v'$ |
| $\left(\dfrac{1}{v}\right)'$ | $-\dfrac{v'}{v^2}\quad (v \neq 0)$ |
| $\left(\dfrac{u}{v}\right)'$ | $\dfrac{u'\,v - u\,v'}{v^2}\quad (v \neq 0)$ |
| $(u^n)'$ | $n\,u^{n-1}\,u'$ |
| $(\sqrt{u})'$ | $\dfrac{u'}{2\sqrt{u}}\quad (u > 0)$ |
:::

## 3. أمثلة تطبيقية

::: example
**📝 المثال 1:** اشتق $f(x) = 3x^4 - 5x^2 + 7x - 2$.

**✅ الحل:**
$$ f'(x) = 12x^3 - 10x + 7 $$
:::

::: example
**📝 المثال 2:** اشتق $g(x) = \dfrac{2x+1}{x-3}$ على $\mathbb{R}\setminus\{3\}$.

**✅ الحل:** نضع $u = 2x+1$ و $v = x-3$، إذن $u' = 2$ و $v' = 1$:

$$ g'(x) = \frac{u'v - uv'}{v^2} = \frac{2(x-3) - (2x+1)\cdot 1}{(x-3)^2} = \frac{-7}{(x-3)^2} $$
:::

::: example
**📝 المثال 3:** اشتق $h(x) = \sqrt{x^2 + 1}$.

**✅ الحل:** نضع $u(x) = x^2+1 > 0$، فإن $u'(x) = 2x$:

$$ h'(x) = \frac{u'(x)}{2\sqrt{u(x)}} = \frac{2x}{2\sqrt{x^2+1}} = \frac{x}{\sqrt{x^2+1}} $$
:::

::: remark
**⚠️ ملاحظات مهمة:**
- لا تنسَ شرط $v \neq 0$ في قاعدة القسمة.
- لا تنسَ شرط $u > 0$ في قاعدة الجذر.
- قبل الاشتقاق، حدِّد دائماً **مجال الاشتقاقية** $D_{f'}$.
:::

## 🎯 خلاصة الدرس

- احفظ جدول المشتقات المرجعية جيداً.
- استعمل قواعد العمليات بترتيب: أوّلاً المجموع، ثم الجداء، ثم القسمة.
- تحقّق دائماً من شروط الاشتقاقية.
