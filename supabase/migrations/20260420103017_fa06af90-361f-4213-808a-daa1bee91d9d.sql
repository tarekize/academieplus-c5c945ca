# درس: التقريب التآلفي وطريقة أولر

نستعمل **العدد المشتق** لإيجاد **تقريب تآلفي** لقيم دالة بجوار نقطة، ثم نطبّق ذلك في **طريقة أولر** للتقريب العددي.

## 1. التقريب التآلفي

::: definition
**تعريف 1.1 — التقريب التآلفي**

إذا كانت $f$ قابلة للاشتقاق عند $x_0$، فمن أجل $h$ قريب من $0$:

$$ f(x_0 + h) \approx f(x_0) + h\,f'(x_0) $$

تُسمى الدالة $h \mapsto f(x_0) + h\,f'(x_0)$ **التقريب التآلفي** للدالة $f$ بجوار $x_0$.
:::

::: graphic
**📈 التفسير البياني**

التقريب التآلفي هو **تعويض المنحنى $\mathcal{C}_f$ بالمماس $(T)$** عند النقطة $x_0$ في مجال صغير حول $x_0$.

كلما كان $h$ أصغر، كان التقريب **أدقّ**.
:::

## 2. مثال تطبيقي

::: example
**📝 المثال:** أعط تقريباً للعدد $\sqrt{4{,}02}$ باستعمال التقريب التآلفي.

**✅ الحل:**

نأخذ $f(x) = \sqrt{x}$ مع $x_0 = 4$ و $h = 0{,}02$.

نحسب:
$$ f(4) = 2, \quad f'(x) = \frac{1}{2\sqrt{x}}, \quad f'(4) = \frac{1}{4} = 0{,}25 $$

ومنه:
$$ \sqrt{4{,}02} = f(4 + 0{,}02) \approx f(4) + 0{,}02 \times f'(4) = 2 + 0{,}02 \times 0{,}25 = 2{,}005 $$

(القيمة الحقيقية: $\sqrt{4{,}02} \approx 2{,}00499$، إذن التقريب ممتاز).
:::

## 3. طريقة أولر

::: definition
**تعريف 3.1 — طريقة أولر**

طريقة أولر هي طريقة عددية لتقريب قيم دالة $f$ تحقّق المعادلة التفاضلية $y' = \varphi(x, y)$ مع شرط ابتدائي $y(x_0) = y_0$.

نختار **خطوة** $h$ صغيرة، ونعرّف المتتالية بـ:

$$ \begin{cases} x_{n+1} = x_n + h \\ y_{n+1} = y_n + h \cdot \varphi(x_n, y_n) \end{cases} $$

تُعطي $y_n$ تقريباً لـ $f(x_n)$.
:::

## 4. مثال على طريقة أولر

::: example
**📝 المثال:** لتكن $f$ تحقّق $f'(x) = 2x$ مع $f(0) = 1$ (إذن $f(x) = x^2 + 1$).

أعط تقريباً لـ $f(0{,}3)$ بطريقة أولر بخطوة $h = 0{,}1$.

**✅ الحل:**

| $n$ | $x_n$ | $y_n$ | $\varphi(x_n,y_n) = 2x_n$ | $y_{n+1} = y_n + 0{,}1 \cdot 2x_n$ |
|---|---|---|---|---|
| $0$ | $0$ | $1$ | $0$ | $1 + 0 = 1$ |
| $1$ | $0{,}1$ | $1$ | $0{,}2$ | $1 + 0{,}02 = 1{,}02$ |
| $2$ | $0{,}2$ | $1{,}02$ | $0{,}4$ | $1{,}02 + 0{,}04 = 1{,}06$ |
| $3$ | $0{,}3$ | $1{,}06$ | — | — |

إذن $f(0{,}3) \approx 1{,}06$ (القيمة الحقيقية: $1{,}09$).
:::

::: remark
**⚠️ ملاحظة:** كلما صغرت الخطوة $h$، تحسّنت دقة التقريب لكن يزداد عدد الحسابات.
:::

## 🎯 خلاصة الدرس

- **التقريب التآلفي:** $f(x_0+h) \approx f(x_0) + h\,f'(x_0)$.
- هندسياً: نعوّض المنحنى بالمماس بجوار $x_0$.
- **طريقة أولر:** خوارزمية تكرارية لتقريب حلول معادلة تفاضلية.
